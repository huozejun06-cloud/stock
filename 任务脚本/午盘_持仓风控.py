import os
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from 工具库.数据工具 import (
    获取指数实时数据, 获取日K线数据, 计算全部技术指标,
    获取资金流向, 获取龙虎榜数据, 获取市场情绪数据,
    检测分时异动,
    判断均线排列, 判断MACD信号, 判断ADX描述,
    判断量价形态, 判断换手率分类, 判断布林形态
)
from datetime import datetime

# ==============================================================================
# 配置：要审计的持仓股票列表（可修改）
# ==============================================================================
持仓列表 = ["601138"]  # 可改为你的持仓，如 ["601138", "000001", "300750"]


def 审计单只股票(股票代码: str):
    """对单只股票进行午盘风险评估"""
    print(f"\n{'='*50}")
    print(f"🔍 审计持仓: {股票代码}")

    # --- 1. 分时异动（最高优先级） ---
    print("  📈 检测分时量价异动...")
    分时 = 检测分时异动(股票代码)
    print(f"    当前价: {分时['当前价']}")
    print(f"    相对黄线: {分时['相对黄线位置']}")
    if 分时['爆量滞涨']:
        print(f"    ⚠️ {分时['异动警告']}")

    # --- 2. 技术面 ---
    print("  📊 分析技术指标...")
    try:
        数据框 = 获取日K线数据(股票代码, 使用缓存=True)
        数据框 = 计算全部技术指标(数据框)
        最新 = 数据框.iloc[-1]
        前一条 = 数据框.iloc[-2] if len(数据框) > 1 else 最新

        均线 = 判断均线排列(最新)
        macd = 判断MACD信号(最新, 前一条)
        adx = 判断ADX描述(最新['ADX'])
        量价 = 判断量价形态(最新, 前一条)
        布林 = 判断布林形态(最新)
        换手 = 判断换手率分类(最新['turnover'])

        print(f"    均线排列: {均线}")
        print(f"    MACD: {macd}")
        print(f"    ADX: {最新['ADX']:.1f}({adx})")
        print(f"    RSI6: {最新['RSI6']:.1f}")
        print(f"    量价: {量价}")
        print(f"    布林: {布林}")
        print(f"    换手: {换手}")

        # 风险评分
        风险分 = 0
        if 最新['close'] < 最新['MA5']:
            风险分 += 3
            print(f"    🔴 高风险：跌破MA5")
        elif 最新['close'] < 最新['MA10']:
            风险分 += 2
            print(f"    🟡 中风险：在MA5-MA10之间")
        else:
            print(f"    🟢 低风险：站上MA5")

        if 分时['相对黄线位置'] == "跌破黄线 ❌":
            风险分 += 2
        if 最新['RSI6'] > 80:
            风险分 += 1
            print(f"    ⚠️ RSI6超买({最新['RSI6']:.1f})")
        elif 最新['RSI6'] < 20:
            风险分 += 1
            print(f"    ⚠️ RSI6超卖({最新['RSI6']:.1f})")

    except Exception as e:
        print(f"    ❌ 技术分析失败: {e}")
        风险分 = 5

    # --- 3. 资金流向 ---
    print("  💰 检查资金流向...")
    资金 = 获取资金流向(股票代码)
    print(f"    主力净流入: {资金['主力净流入']}")
    print(f"    超大单: {资金['超大单净流入']}")
    print(f"    连续资金方向: {资金['连续资金方向']}")

    # --- 4. 龙虎榜 ---
    print("  🐅 审计龙虎榜...")
    龙虎榜 = 获取龙虎榜数据(股票代码)
    if 龙虎榜["有龙虎榜"]:
        print(f"    上榜日期: {龙虎榜['上榜日期']}")
        print(f"    净买入额: {龙虎榜['净买入额']}")
        print(f"    {龙虎榜['风险提示']}")
    else:
        print(f"    近5日无龙虎榜数据")

    # --- 5. 综合评级 ---
    print(f"\n{'='*50}")
    if 风险分 >= 5:
        print(f"🔴 【综合评级: 高风险】建议减仓或清仓")
    elif 风险分 >= 3:
        print(f"🟡 【综合评级: 中风险】建议持有观察")
    else:
        print(f"🟢 【综合评级: 低风险】趋势健康")

    if 分时['异动警告']:
        print(f"⚠️ 盘中警告: {分时['异动警告']}")

    return 风险分


def 执行午盘任务():
    """午盘主流程"""
    print("=" * 60)
    print("🌤 【午盘任务】持仓风险评估 & 分时异动监控")
    print(f"   执行时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    # --- 大盘环境 ---
    print("\n📡 获取大盘环境...")
    指数数据 = 获取指数实时数据()
    if "s_sh000001" in 指数数据:
        d = 指数数据["s_sh000001"]
        print(f"   上证指数: {d['最新价']:.2f} ({d['涨跌幅']:+.2f}%)")

    # --- 市场情绪 ---
    print("\n📊 扫描市场情绪...")
    情绪 = 获取市场情绪数据()
    print(f"   情绪评级: {情绪['情绪评级']}")
    print(f"   最高连板: {情绪['最高连板']}")
    print(f"   涨停/跌停: {情绪['涨停家数']}/{情绪['跌停家数']}")

    # --- 逐一审计持仓 ---
    print(f"\n📋 共 {len(持仓列表)} 只持仓需要审计")
    总风险 = 0
    for 代码 in 持仓列表:
        风险 = 审计单只股票(代码)
        总风险 += 风险

    # --- 汇总 ---
    print(f"\n{'='*60}")
    print(f"📊 持仓汇总：{len(持仓列表)}只 | 平均风险分: {总风险/len(持仓列表):.1f}")
    if 情绪['情绪评级'] == '退潮':
        print("⚠️ 市场处于退潮期，建议降低总仓位")
    elif 情绪['情绪评级'] == '活跃':
        print("✅ 市场情绪活跃，可积极操作")
    print("=" * 60)


if __name__ == "__main__":
    执行午盘任务()
