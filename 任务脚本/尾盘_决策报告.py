def 判断今日是否可交易(情绪数据: dict) -> tuple:
    """
    市场环境过滤器
    返回: (是否可以交易: bool, 原因: str)
    """
    def _num(v, default=0):
        try:
            return int(float(str(v if v else default)))
        except (ValueError, TypeError):
            return default

    涨停 = _num(情绪数据.get('涨停家数', 0))
    跌停 = _num(情绪数据.get('跌停家数', 0))
    全市场 = _num(情绪数据.get('全市场股票', 1))
    上涨 = _num(情绪数据.get('上涨家数', 0))
    上涨比例 = 上涨 / 全市场 if 全市场 > 0 else 0
    
    # 禁止交易条件（任一触发就不做）
    if 涨停 < 30:
        return False, f"涨停仅{涨停}家，情绪冰点，不适合交易"
    if 跌停 > 50:
        return False, f"跌停多达{跌停}家，系统性风险，应空仓"
    if 上涨比例 < 0.3:
        return False, f"上涨比例仅{上涨比例:.1%}，普跌行情，不宜入场"
    
    # 可交易
    if 涨停 >= 80:
        return True, f"市场活跃（涨停{涨停}家），可积极选股"
    else:
        return True, f"市场正常（涨停{涨停}家），可谨慎选股"


