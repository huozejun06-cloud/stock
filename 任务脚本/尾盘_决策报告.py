def 尾盘兜底检查():
    """
    检查预选池中是否有尾盘兜底机会
    ★ 铁律1：检查 status 字段，已闪击的跳过
    条件：在支撑位附近（-1% ~ +1%）
    """
    import json
    from datetime import datetime

    try:
        with open(os.path.join(CACHE_DIR, '预选池.json'), 'r') as f:
            预选池 = json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return []

    现在 = datetime.now()
    if 现在.hour < 14 or (现在.hour == 14 and 现在.minute < 30):
        return []  # 还没到尾盘时间

    兜底机会 = []
    for 股 in 预选池['预选股']:
        # ★ 铁律1：检查状态，已闪击的跳过
        if 股['status'] != 'pending':
            continue

        # 获取最新行情
        mgr = get_manager()
        df = mgr.get_stock_realtime([股['代码']])
        if df.empty:
            continue

        当前价 = df.iloc[0].get('最新价', 0)
        支撑位1 = 股.get('关键价位', {}).get('支撑位1', 0)

        if 支撑位1 <= 0:
            continue

        偏离 = (当前价 - 支撑位1) / 支撑位1 * 100

        # 条件：在支撑位附近（-1% ~ +1%）
        if not (-1 <= 偏离 <= 1):
            continue

        兜底机会.append({
            '代码': 股['代码'],
            '名称': 股['名称'],
            '当前价': 当前价,
            '支撑位': 支撑位1,
            '偏离%': round(偏离, 2),
            '判定': '尾盘兜底买点' if 偏离 > -1 else '接近支撑位可关注',
        })

    return 兜底机会


def 执行尾盘兜底():
    """
    尾盘兜底主流程
    """
    print("=" * 60)
    print("🟣 【尾盘兜底】14:30-15:00 预选池支撑位兜底检查")
    print(f"   执行时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    兜底机会 = 尾盘兜底检查()

    if not 兜底机会:
        print("  ⏸ 今日无尾盘兜底机会")
        return []

    print(f"\n  🎯 发现 {len(兜底机会)} 个尾盘兜底机会:")
    for 机会 in 兜底机会:
        print(f"    {机会['代码']} {机会['名称']} | "
              f"当前价{机会['当前价']:.2f} | "
              f"支撑位{机会['支撑位']} | "
              f"偏离{机会['偏离%']:+.2f}% | "
              f"{机会['判定']}")

    # ★ 铁律1：更新预选池状态
    try:
        from 任务脚本.早盘_闪击 import 更新预选池状态
        for 机会 in 兜底机会:
            更新预选池状态(机会['代码'], 'downdied', {
                '兜底买入价': 机会['当前价'],
                '兜底时间': datetime.now().strftime('%H:%M'),
            })
    except Exception as e:
        print(f"  ⚠️ 更新预选池状态失败: {e}")

    return 兜底机会


