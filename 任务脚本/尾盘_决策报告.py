def 调用DeepSeek(提示词: str) -> str:
    """调用DeepSeek API生成市场解读"""
    headers = {
        "Authorization": f"Bearer {DEEPSEEK_API_KEY}",
        "Content-Type": "application/json"
    }

    payload = {
        "model": "deepseek-reasoner",
        "messages": [
            {"role": "system", "content": "你是一位拥有20年经验的机构级首席交易员，擅长T+1短线交易和尾盘决策。请基于数据给出专业、客观的市场解读，但不要代替交易决策引擎做最终决策。"},
            {"role": "user", "content": 提示词}
        ],
        "temperature": 0.3,
        "max_tokens": 2000
    }

    try:
        print("  🧠 正在调用 DeepSeek-R1 推理引擎（仅用于解读数据）...")
        resp = requests.post(DEEPSEEK_API_URL, headers=headers, json=payload, timeout=120)
        if resp.status_code == 200:
            result = resp.json()
            return result['choices'][0]['message']['content']
        else:
            return f"❌ API调用失败: HTTP {resp.status_code}\n{resp.text[:500]}"
    except Exception as e:
        return f"❌ API调用异常: {e}"


