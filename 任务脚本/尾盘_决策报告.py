def 检查五条红线(数据: dict) -> dict:
    """
    尾盘五条硬性过滤规则：
    ① 分时线站上黄线（收盘≥开盘）
    ② 未跌破关键支撑（最低价未破开盘价-ATR×0.5）
    ③ 成交量未恐慌砸盘（不低于5日均量的30%）
    ④ 主力大单净值为正
    ⑤ 15分钟MACD未死叉（用日线DIF>DEA近似）
    """
    最新 = 数据["最新"]
    前一条 = 数据["前一条"]
    数据框 = 数据["数据框"]
    资金 = 数据["资金流向"]
    分时 = 数据["分时异动"]

    结果 = {
        "规则1_站上黄线": {"通过": False, "详情": ""},
        "规则2_未破支撑": {"通过": False, "详情": ""},
        "规则3_量能正常": {"通过": False, "详情": ""},
        "规则4_主力为正": {"通过": False, "详情": ""},
        "规则5_MACD未死叉": {"通过": False, "详情": ""},
        "通过数": 0,
        "总规则": 5,
        "允许建仓": False
    }

    # 规则1：分时线站上黄线（收盘≥开盘）
    if 最新['close'] >= 最新['open']:
        结果["规则1_站上黄线"]["通过"] = True
        结果["规则1_站上黄线"]["详情"] = f"收盘{最新['close']:.2f}≥开盘{最新['open']:.2f} ✅"
    else:
        结果["规则1_站上黄线"]["详情"] = f"收盘{最新['close']:.2f}<开盘{最新['open']:.2f} ❌"

    # 规则2：未跌破关键支撑
    关键支撑 = 最新['open'] - 最新['ATR14'] * 0.5
    if 最新['low'] >= 关键支撑:
        结果["规则2_未破支撑"]["通过"] = True
        结果["规则2_未破支撑"]["详情"] = f"最低{最新['low']:.2f}≥支撑{关键支撑:.2f}(开盘-ATR×0.5) ✅"
    else:
        结果["规则2_未破支撑"]["详情"] = f"最低{最新['low']:.2f}<支撑{关键支撑:.2f} ❌"

    # 规则3：成交量未恐慌砸盘
    # 🔧 修复：确保两边单位完全对齐为"股"
    # 新浪行情中成交量单位为"手"(1手=100股)，而技术指标的volume列单位为"股"
    # 如果成交量数值较小(<50万手)，说明是"股"单位，需要×100转成"股" 
    量MA5 = 数据框['volume'].tail(5).mean()
    实时成交量_股 = float(最新['volume'])
    if 实时成交量_股 < 500000:
        实时成交量_股 = 实时成交量_股 * 100
    if 实时成交量_股 >= 量MA5 * 0.3:
        结果["规则3_量能正常"]["通过"] = True
        结果["规则3_量能正常"]["详情"] = f"成交量{实时成交量_股:.0f}股≥阈值{量MA5*0.3:.0f}股(5日均量×30%) ✅"
    else:
        结果["规则3_量能正常"]["详情"] = f"成交量{实时成交量_股:.0f}股<阈值{量MA5*0.3:.0f}股 ❌"

    # 规则4：主力大单净值为正
    try:
        主力净流入 = float(资金["主力净流入"])
        if 主力净流入 > 0:
            结果["规则4_主力为正"]["通过"] = True
            结果["规则4_主力为正"]["详情"] = f"主力净流入{主力净流入:.0f}>0 ✅"
        else:
            结果["规则4_主力为正"]["详情"] = f"主力净流入{主力净流入:.0f}≤0 ❌"
    except (ValueError, TypeError):
        结果["规则4_主力为正"]["详情"] = "主力净流入数据不可用 ❌"

    # 规则5：MACD未死叉
    if 最新['DIF'] > 最新['DEA']:
        结果["规则5_MACD未死叉"]["通过"] = True
        结果["规则5_MACD未死叉"]["详情"] = f"DIF{最新['DIF']:.2f}>DEA{最新['DEA']:.2f} ✅"
    else:
        结果["规则5_MACD未死叉"]["详情"] = f"DIF{最新['DIF']:.2f}≤DEA{最新['DEA']:.2f} ❌"

    # 统计
    结果["通过数"] = sum(1 for k in 结果 if k.startswith("规则") and 结果[k]["通过"])
    结果["允许建仓"] = 结果["通过数"] >= 4

    return 结果


