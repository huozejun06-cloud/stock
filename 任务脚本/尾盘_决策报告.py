def 最终裁决(候选股分析列表, 大盘数据, 情绪数据):
    """
    攻防一体的最终裁决系统
    整合市场环境 + 决策引擎输出
    """
    print("\n" + "=" * 60)
    print("⚖️ 执行最终裁决...")
    print("=" * 60)
    分数 = 100
    理由 = []

    # 防守侧1：市场环境检查
    可交易, 环境原因 = 判断今日是否可交易(情绪数据)
    if not 可交易:
        分数 -= 50
        理由.append(f"🔴 市场环境禁止交易: {环境原因}")
    else:
        理由.append(f"🟢 市场环境可交易")

    # 防守侧2：统计通过引擎积极评级的比例（适配10只候选股）
    积极评级数 = sum(1 for d in 候选股分析列表 if d.get('决策信号') in ['A', 'B'])
    A级数 = sum(1 for d in 候选股分析列表 if d.get('决策信号') == 'A')
    D级数 = sum(1 for d in 候选股分析列表 if d.get('决策信号') == 'D')
    总候选数 = len(候选股分析列表)

    if A级数 >= 1:
        分数 += 20
        理由.append(f"🟢 有{A级数}只候选股被引擎评为A级(强烈做多)")
    elif 积极评级数 >= 1:
        分数 += 10
        理由.append(f"🟡 有{积极评级数}只候选股获得积极评级(A/B)，可轻仓试探")
    else:
        分数 -= 15
        理由.append(f"🔴 无候选股通过引擎的积极评级")

    if D级数 > 总候选数 * 0.5:
        分数 -= 20
        理由.append(f"🔴 {D级数}/{总候选数}候选股被引擎评为D级(禁止交易)")


    # 防守侧3：大盘方向
    指数上涨数 = sum(1 for k in ['上证涨跌', '深证涨跌', '创业板涨跌'] if 大盘数据.get(k, 0) > 0)
    if 指数上涨数 >= 2:
        分数 += 10
        理由.append(f"🟢 {指数上涨数}/3指数上涨，大势向好")
    elif 指数上涨数 == 0:
        分数 -= 15
        理由.append(f"🔴 三大指数全部下跌，系统性风险")

    # 最终裁决
    if 分数 >= 80:
        决策, 仓位系数 = '🟢 执行交易', 1.0
    elif 分数 >= 60:
        决策, 仓位系数 = '🟡 减仓执行', 0.5
    elif 分数 >= 40:
        决策, 仓位系数 = '🟠 仅观察', 0.0
    else:
        决策, 仓位系数 = '🔴 取消交易', 0.0

    理由.append(f"📊 总得分：{分数}/100")

    print(f"\n  📊 最终裁决:")
    for r in 理由:
        print(f"    {r}")
    print(f"\n  🏆 决策：{决策} | 仓位系数：{仓位系数}")

    return {
        '决策': 决策,
        '分数': 分数,
        '仓位系数': 仓位系数,
        '理由': 理由,
    }


