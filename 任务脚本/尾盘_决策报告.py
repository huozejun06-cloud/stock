def screen_candidates(market_df, hot_boards, board_map):
    """
    优化版候选股筛选
    1. 取热门板块前5
    2. 从每个板块中取涨幅排名前3的个股（不限具体涨幅，确保选到板块内最强）
    3. 计算相对强度 RS = 个股涨幅 - 板块平均涨幅，只保留 RS > 0 的个股
    4. 排除 ST/退市股、冷门号段（605xxx、001xxx、003xxx、301xxx、43xxx、83xxx、87xxx）
    5. 按涨幅排序，取 Top10（不硬凑）
    """
    print("\n🔍 全市场候选股筛选（新逻辑：板块内前3 + RS相对强度过滤 + 号段过滤）...")
    if market_df.empty:
        return []
    
    # ⛔ 冷门号段前缀（冷门次新 + 北交所）
    冷门号段前缀 = ('605', '001', '003', '301', '43', '83', '87')
    
    print(f"  📊 热门板块(前5): {[b['名称'] for b in hot_boards[:5]]}")
    
    candidates = []
    
    for board in hot_boards[:5]:
        board_name = board['名称']
        board_avg_pct = board['涨跌幅']  # 板块平均涨幅
        codes = board_map.get(board_name, [])
        
        # 获取该板块成分股的实时数据
        board_stocks = []
        for code in codes:
            code_str = str(code).zfill(6)
            stock = market_df[market_df['代码'].astype(str).str.zfill(6) == code_str]
            if stock.empty:
                continue
            row = stock.iloc[0]
            name = str(row.get('名称', ''))
            pct = row.get('涨跌幅', 0)
            
            # 排除 ST/退市
            if 'ST' in name or '退' in name:
                continue
            
            # ⛔ 冷门号段过滤
            if code_str.startswith(冷门号段前缀):
                continue
            
            # 计算相对强度 RS = 个股涨幅 - 板块平均涨幅
            rs = pct - board_avg_pct
            
            board_stocks.append({
                '代码': code,
                '名称': name,
                '涨跌幅': pct,
                '板块': board_name,
                '板块内排名': 0,  # 稍后计算
                'RS': round(rs, 2),
            })
        
        # 按涨幅排序，取板块内前3名
        board_stocks.sort(key=lambda x: x['涨跌幅'], reverse=True)
        for i, s in enumerate(board_stocks[:3]):
            s['板块内排名'] = i + 1
            # 只保留 RS > 0 的个股
            if s['RS'] > 0:
                candidates.append(s)
    
    # 按涨幅排序
    candidates.sort(key=lambda x: x['涨跌幅'], reverse=True)
    
    # 取 Top10（不硬凑）
    top10 = candidates[:10]
    
    if top10:
        print(f"\n  🏆 精选 Top{len(top10)} 候选股（RS相对强度过滤）:")
        for i, c in enumerate(top10, 1):
            print(f"    {i}. {c['代码']} {c['名称']}: {c['涨跌幅']:+.2f}% | 板块:{c['板块']} | RS:{c['RS']:+.2f}%")
    else:
        print("    ⚠️ 今日无符合条件的候选股")
    
    return top10


