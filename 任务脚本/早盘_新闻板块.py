import sys
import os
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from 工具库.数据工具 import (
    获取指数实时数据, 动态板块扫描, 动态板块选黑马,
    获取市场情绪数据, 获取日K线数据, 计算全部技术指标,
    获取资金流向, 判断均线排列, 判断MACD信号,
    快捷扫描全市场个股, 清除缓存
)
from datetime import datetime


def 执行早盘任务():
    """
    早盘主流程（全自动动态雷达版）：
    指数 → 全市场动态板块扫描(得分=涨跌幅×0.6+3日资金×0.4) → Top3锁定 → 黑马筛选 → 写入watchlist
    零硬编码！零人工干预！完全依靠系统自动识别。
    """
    print("=" * 60)
    print("🌅 【早盘任务】全自动动态板块雷达 v2.0")
    print(f"   执行时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"   口号：让市场的无形之手自己选出当下最赚钱的行业！")
    print("=" * 60)

    # --- 1. 指数行情 ---
    print("\n📡 正在获取指数实时行情...")
    指数数据 = 获取指数实时数据()

    指数行 = []
    for 代码, 名称 in [
        ("s_sh000001", "上证指数"), ("s_sz399001", "深证成指"),
        ("s_sz399006", "创业板指"), ("s_sh000688", "科创50")
    ]:
        if 代码 in 指数数据:
            d = 指数数据[代码]
            指数行.append(f"  {名称}: {d['最新价']:.2f} ({d['涨跌幅']:+.2f}%)")

    上证涨跌 = 指数数据.get("s_sh000001", {}).get('涨跌幅', 0.0)

    print("\n📊 【四大指数早盘行情】")
    for 行 in 指数行:
        print(行)

    # --- 2. 🔥 全自动动态板块雷达扫描 ---
    print("\n🏭 正在启动全自动动态板块雷达...")
    print(f"   评分公式：得分 = 涨跌幅归一化(Min-Max→[0,1]) × 0.6 + 3日资金净额归一化(Min-Max→[0,1]) × 0.4")
    print(f"   ⚠️ 注意：涨跌幅原始值(如1.36%)和资金净额原始值(如-181亿)不能直接相乘相加，")
    print(f"      因为量纲差异巨大(亿 vs %)。必须先 Min-Max 归一化到 [0,1] 区间再加权。")
    板块雷达 = 动态板块扫描()

    if 板块雷达["成功"]:
        print(f"\n{板块雷达['文本']}")
        print(f"\n  🏆 【系统自动锁定的Top3热门板块】")
        板块列表 = 板块雷达["板块列表"]
        Top3 = 板块雷达["Top3"]
        for i, b in enumerate(Top3):
            领涨股信息 = f" | 领涨: {b.get('领涨股', 'N/A')}" if b.get('领涨股') else ""
            print(f"    🥇{i+1}. {b['名称']} — 涨跌幅{b['涨跌幅']:+.2f}% | "
                  f"3日净额{b['资金净额']:+.2f}亿 | 得分{b['得分']:.3f}{领涨股信息}")
    else:
        print(f"\n  ⚠️ 板块雷达扫描失败: {板块雷达['文本']}")

    # --- 3. 市场情绪扫描 ---
    print("\n📊 正在扫描全市场短线情绪...")
    情绪 = 获取市场情绪数据()
    print(f"   涨停家数: {情绪['涨停家数']}")
    print(f"   跌停家数: {情绪['跌停家数']}")
    print(f"   最高连板: {情绪['最高连板']}")
    print(f"   昨日涨停表现: {情绪['昨日涨停表现']}")
    print(f"   赚钱效应: {情绪['赚钱效应']}")
    print(f"   情绪评级: {情绪['情绪评级']}")

    # --- 4. 🔥 联动精选黑马股：从Top3板块中自动筛选 ---
    print("\n🐎 正在从Top3热门板块中精选黑马股...")
    print(f"   筛选条件：量比>0.8 + 5日均线多头排列 + 站上MA5")
    
    # 🔥 情绪风控过滤：冰点/退潮不推荐买入
    情绪评级 = 情绪['情绪评级']
    情绪为危险 = 情绪评级 in ('退潮', '冰点', '恐慌')
    if 情绪为危险:
        print(f"    ⛔ 【情绪风控触发】情绪评级为'{情绪评级}'，禁止输出买入推荐！")
        print(f"    📌 黑马池清空，仅保留技术面观察池供跟踪参考")
    
    黑马股 = []
    黑马选股文本 = ""
    观察池 = []  # 仅供观察
    观察池文本 = ""

    if 板块雷达["成功"] and Top3:
        print(f"   雷达数据源: {板块雷达['数据源']}")
        原始黑马池 = 动态板块选黑马(Top3, 数据源=板块雷达['数据源'], 取前几名=5)

        if 原始黑马池:
            if 情绪为危险:
                # 🔥 情绪退潮：清空买入推荐，但保留观察池
                观察池 = 原始黑马池  # 满足技术条件的马放入观察池
                黑马股 = []  # 买入池置空
                黑马选股文本 = "  ⛔ **暂无推荐（情绪退潮，谨慎观望）**"

                观察池文本_list = []
                for i, s in enumerate(观察池):
                    代码 = s.get('代码', '')
                    名称 = s['名称']
                    板块 = s['板块']
                    技术分 = s.get('技术分', 0)
                    涨跌幅 = s.get('涨跌幅', 0)
                    观察池文本_list.append(
                        f"  {i+1}. ~~{名称}({代码})~~ — {板块} | "
                        f"涨跌幅:{涨跌幅:+.2f}% | 技术分:{技术分} *仅供观察*"
                    )
                观察池文本 = "\n".join(观察池文本_list)
                print(f"    📋 系统自动精选黑马股（已清空）：")
                print(黑马选股文本)
                print(f"    📌 观察池 {len(观察池)} 只（不构成买入建议）：")
                print(观察池文本)
            else:
                # ✅ 情绪正常/活跃：正常输出买入推荐
                黑马股 = 原始黑马池
                黑马选股文本_list = []
                for i, s in enumerate(黑马股):
                    代码 = s.get('代码', '')
                    名称 = s['名称']
                    板块 = s['板块']
                    技术分 = s.get('技术分', 0)
                    涨跌幅 = s.get('涨跌幅', 0)
                    均线多头 = '✅' if s.get('均线多头') else '❌'
                    站上MA5 = '✅' if s.get('站上MA5') else '❌'
                    黑马选股文本_list.append(
                        f"  {i+1}. **{名称}({代码})** — {板块} | "
                        f"涨跌幅:{涨跌幅:+.2f}% | 均线多头:{均线多头} | "
                        f"站上MA5:{站上MA5} | 技术分:{技术分}"
                    )
                黑马选股文本 = "\n".join(黑马选股文本_list)
                print(f"\n  📋 系统自动精选黑马股：")
                print(黑马选股文本)
        else:
            黑马选股文本 = "  系统未能从Top3板块中筛选出符合条件的黑马股（数据源不可用或条件过严）"
            print(f"\n  📋 {黑马选股文本}")
    else:
        黑马选股文本 = "  板块雷达数据暂缺，无法联动精选黑马股"
        print(f"\n  📋 {黑马选股文本}")

    # --- 5. 写入 watchlist.md ---
    时间戳 = datetime.now().strftime('%Y-%m-%d %H:%M')

    # 板块雷达文本
    if 板块雷达["成功"]:
        板块文本 = 板块雷达["文本"]
    else:
        板块文本 = "全自动动态板块扫描暂不可用"

    # Top3文本
    if 板块雷达["成功"] and Top3:
        Top3文本行 = "\n".join([
            f"  {i+1}. **{b['名称']}**: 涨跌幅{b['涨跌幅']:+.2f}% | "
            f"3日净额{b['资金净额']:+.2f}亿 | 得分{b['得分']:.3f}"
            for i, b in enumerate(Top3)
        ])
        Top3块 = f"## 🏆 系统自动锁定 Top3 热门板块\n{Top3文本行}"
    else:
        Top3块 = "## 🏆 系统自动锁定 Top3 热门板块\n  数据暂不可用"

    # 黑马文本
    if 黑马股:
        黑马块 = f"## 🐎 系统精选黑马股（{len(黑马股)}只）\n{黑马选股文本}"
    elif 情绪为危险 and 观察池:
        黑马块 = f"## 🐎 系统精选黑马股\n{黑马选股文本}\n\n### 📌 仅供观察（不构成买入建议）\n{观察池文本}\n"
    else:
        黑马块 = "## 🐎 系统精选黑马股\n  ⛔ 今日无达标黑马（宁缺毋滥：所有候选股均未通过均线多头+站上MA5双重硬性门槛）"


    # 🔥 审计警告：数据完整性检查
    数据警告 = 情绪.get('样本警告', '')
    if 数据警告:
        print(f"    🚨 {数据警告}")

    # 操作建议
    if 情绪['情绪评级'] == '活跃':
        操作建议 = "可积极操作（情绪活跃，赚钱效应强）"
    elif 情绪['情绪评级'] == '退潮':
        操作建议 = "谨慎观望（情绪退潮，亏钱效应明显）"
    else:
        操作建议 = "正常操作（情绪中性，精选个股）"

    数据警告块 = ""
    if 数据警告:
        数据警告块 = f"\n## 🚨 数据完整性警告\n> **{数据警告}**\n> 当前输出仅供参考，选股结果可能不可信！\n"

    内容 = f"""# 📋 全自动动态板块雷达报告（{时间戳}）

{数据警告块}

## 📊 四大指数
{chr(10).join(指数行)}

## 🔍 全市场行业板块扫描
{板块文本}

{Top3块}

## 📈 市场情绪
| 指标 | 数值 |
|------|------|
| 涨停家数 | {情绪['涨停家数']} |
| 跌停家数 | {情绪['跌停家数']} |
| 最高连板 | {情绪['最高连板']} |
| 昨日涨停表现 | {情绪['昨日涨停表现']} |
| 赚钱效应 | {情绪['赚钱效应']} |
| 情绪评级 | {情绪['情绪评级']} |

## 🎯 今日关注要点
- 大盘趋势：{"偏多" if 上证涨跌 > 0 else "偏空"}（上证{上证涨跌:+.2f}%）
- 短线情绪：{情绪['情绪评级']}
- 操作建议：{操作建议}

{黑马块}

---
*由全自动动态板块雷达系统自动生成 | 零硬编码 · 纯数据驱动*
"""
    watchlist路径 = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "watchlist.md")
    with open(watchlist路径, "w", encoding="utf-8") as f:
        f.write(内容)

    print(f"\n✅ 系统已自动更新至: watchlist.md")
    print("=" * 60)
    print(f"🏁 全自动动态板块雷达 v2.0 运行完毕！")
    print(f"   📊 扫描行业板块: {len(板块雷达.get('板块列表', []))}个")
    print(f"   🏆 锁定Top3: {', '.join([b['名称'] for b in Top3]) if Top3 else 'N/A'}")
    print(f"   🐎 精选黑马: {len(黑马股)}只")
    print("=" * 60)


if __name__ == "__main__":
    执行早盘任务()

