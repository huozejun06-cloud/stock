def 判断量价结构(最新: dict, 前一条: dict) -> dict:
    """
    判断量价结构：
    放量突破、放量滞涨（危险）、缩量回调（健康）、无量上涨（诱多）
    """
    涨跌幅 = 最新.get('pct_chg', 0)
    volume = 最新.get('volume', 0)
    前volume = 前一条.get('volume', 0)
    量MA5 = 最新.get('量MA5', 0)

    量放量 = 前volume > 0 and volume > 前volume * 1.3
    量缩量 = 前volume > 0 and volume < 前volume * 0.7
    量大突破量 = pd.notna(量MA5) and 量MA5 > 0 and volume > 量MA5 * 1.5

    类型 = "正常"
    风险 = "低"

    if 涨跌幅 > 3 and (量放量 or 量大突破量):
        类型 = "放量突破"
        风险 = "低"
    elif abs(涨跌幅) < 2 and 量放量:
        类型 = "放量滞涨"
        风险 = "高"
    elif 涨跌幅 < 0 and 量缩量:
        类型 = "缩量回调"
        风险 = "低"
    elif 涨跌幅 > 0 and 量缩量:
        类型 = "无量上涨"
        风险 = "中"
    elif 涨跌幅 < -3 and 量放量:
        类型 = "放量下跌"
        风险 = "高"

    return {
        "类型": 类型,
        "风险": 风险,
        "涨跌幅": 涨跌幅,
        "量比": f"{volume/前volume:.2f}" if 前volume > 0 else "N/A",
        "描述": f"{类型}（{风险}风险）"
    }


