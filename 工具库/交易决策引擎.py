def 判断市场状态(数据框: pd.DataFrame, 最新: dict) -> dict:
    """
    判断当前市场状态：
    强趋势上涨、弱趋势上涨、震荡市、下跌趋势、系统性风险期
    """
    if 数据框 is None or len(数据框) < 60:
        return {"状态": "数据不足", "置信度": 0, "依据": "K线数据不足60根"}

    close = 数据框['close']
    ma20 = 最新.get('MA20', close.mean())
    ma60 = 最新.get('MA60', close.mean())
    adx = 最新.get('ADX', 0)
    bias20 = 最新.get('BIAS20', 0)
    最新价 = 最新['close']

    # 检查系统性风险：MA60持续下降 + ADX高 + BIAS深度负值
    ma60_趋势 = '震荡'
    if 'MA60' in 数据框.columns and len(数据框) >= 20:
        try:
            ma60_趋势 = '上升' if ma60 > 数据框['MA60'].iloc[-20] else '下降'
        except (IndexError, KeyError):
            ma60_趋势 = '震荡'

    # 判断
    依据 = []

    # 强趋势上涨：MA60向上 + 股价>MA20>MA60 + ADX>25
    if (最新价 > ma20 > ma60 and
            adx > ADX_中等趋势 and
            bias20 > 0 and
            ma60 > 数据框['MA60'].iloc[-20]):
        状态 = "强趋势上涨"
        置信度 = min(100, int(adx * 2 + 20))
        依据 = [f"MA20({ma20:.2f})>MA60({ma60:.2f})多头排列",
               f"ADX={adx:.1f}确认趋势强度",
               f"BIAS20={bias20:+.2f}%正乖离"]

    # 弱趋势上涨：股价>MA60但MA20可能缠绕
    elif 最新价 > ma60 and adx > 15:
        状态 = "弱趋势上涨"
        置信度 = min(80, int(adx * 2))
        依据 = [f"股价{最新价:.2f}站上MA60{ma60:.2f}",
               f"ADX={adx:.1f}趋势偏弱",
               f"MA20({ma20:.2f})与MA60({ma60:.2f})关系待确认"]

    # 下跌趋势：股价<MA20<MA60 + ADX>25
    elif 最新价 < ma20 < ma60 and adx > ADX_中等趋势:
        状态 = "下跌趋势"
        置信度 = min(100, int(adx * 2 + 10))
        依据 = [f"空头排列: 股价{最新价:.2f}<MA20{ma20:.2f}<MA60{ma60:.2f}",
               f"ADX={adx:.1f}确认下跌强度"]

    # 系统性风险期：MA60下降 + BIAS深度负值 + 放量下跌
    elif (bias20 < BIAS_超跌 and
          adx > ADX_强趋势 and
          最新价 < ma20):
        状态 = "系统性风险期"
        置信度 = 95
        依据 = [f"BIAS20={bias20:+.2f}%深度超跌",
               f"ADX={adx:.1f}强趋势下行",
               f"系统性风险预警"]

    # 震荡市（默认）
    else:
        状态 = "震荡市"
        置信度 = 60
        依据 = [f"股价{最新价:.2f}在MA20({ma20:.2f})附近缠绕",
               f"ADX={adx:.1f}<25无明确趋势"]

    return {
        "状态": 状态,
        "置信度": 置信度,
        "依据": 依据,
        "判断": f"市场状态 = {状态}（概率{置信度}%）"
    }


