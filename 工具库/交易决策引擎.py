# ==============================================================================


def rule_19_w_bottom_confirm(股票代码: str) -> dict:
    """
    规则19：分时W底确认（新增规则）

    逻辑：
    1. 调用 获取5分钟K线(股票代码) 获取当日5分钟K线
    2. 调用 detect_w_bottom(kline) 运行W底检测
    3. 根据评分调整总评：
       - 评分≥90（完美W底）：强烈确认，加分30%，标记"分时W底确认"
       - 评分≥70（有效W底）：加分15%
       - 评分<70：不调整

    返回：
    {
        "生效": True/False,
        "调整幅度": 0.3,  # 或0.15、0
        "W底详情": {...}   # detect_w_bottom 的返回
    }
    """
    try:
        from 工具库.分钟线数据 import 获取5分钟K线
        from 工具库.形态识别 import detect_w_bottom

        kline = 获取5分钟K线(股票代码)
        if not kline:
            return {"生效": False, "调整幅度": 0, "W底详情": {}}

        w_result = detect_w_bottom(kline)
        score = w_result.get("score", 0)

        if score >= 90:
            return {
                "生效": True,
                "调整幅度": 0.3,
                "W底详情": w_result,
            }
        elif score >= 70:
            return {
                "生效": True,
                "调整幅度": 0.15,
                "W底详情": w_result,
            }
        else:
            return {
                "生效": False,
                "调整幅度": 0,
                "W底详情": w_result,
            }
    except Exception as e:
        return {"生效": False, "调整幅度": 0, "W底详情": {"error": str(e)}}


