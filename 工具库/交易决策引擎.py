def level1_主力资金_板块_大盘(最新: dict, 资金流向: dict,
                              板块数据: dict, 大盘数据: dict) -> dict:
    """
    🥇 Level 1：决定能不能交易
    检查：主力资金、板块强度+龙头状态、大盘方向
    任一为负 → 禁止交易概率大幅提升
    """
    结果 = {
        "允许交易": True,
        "警报": [],
        "严重度": 0,  # 0=安全 1=警告 2=危险
    }

    # 1. 主力资金检查
    主力净流入 = 0
    try:
        主力净流入 = float(资金流向.get("主力净流入", 0))
    except (ValueError, TypeError):
        主力净流入 = 0

    if 主力净流入 < -50000000:  # 主力流出>5000万
        结果["警报"].append(f"主力资金大幅流出{主力净流入/10000:.0f}万")
        结果["严重度"] += 1
    elif 主力净流入 < 0:
        结果["警报"].append(f"主力资金小幅流出{主力净流入/10000:.0f}万")
        结果["严重度"] += 0  # 轻微警告

    # 2. 板块强度
    if 板块数据:
        板块涨跌幅 = 板块数据.get('涨跌幅', 0)
        if 板块涨跌幅 < -2:
            结果["警报"].append(f"所属板块涨幅{板块涨跌幅:+.2f}%，板块偏弱")
            结果["严重度"] += 1
        elif 板块涨跌幅 < 0:
            结果["警报"].append(f"所属板块涨幅{板块涨跌幅:+.2f}%，板块微跌")
            结果["严重度"] += 0

    # 3. 大盘方向
    大盘_OK = True
    上证涨跌 = 大盘数据.get('上证涨跌', 0)
    深证涨跌 = 大盘数据.get('深证涨跌', 0)
    创业板涨跌 = 大盘数据.get('创业板涨跌', 0)
    指数上涨数 = sum(1 for v in [上证涨跌, 深证涨跌, 创业板涨跌] if v > 0)

    if 指数上涨数 == 0:
        结果["警报"].append("三大指数全部下跌，系统性风险")
        结果["严重度"] += 2
        大盘_OK = False
    elif 指数上涨数 == 1:
        结果["警报"].append("仅{指数上涨数}/3指数上涨，大势分化")

    # 最终判断
    if 结果["严重度"] >= 3:
        结果["允许交易"] = False
        结果["判定"] = "禁止交易（Level 1多重风险）"
    elif 结果["严重度"] >= 1:
        结果["判定"] = "谨慎交易（Level 1存在警示）"
    else:
        结果["判定"] = "允许交易（Level 1通过）"

    return 结果


def level2_方向判断(最新: dict) -> dict:
    """
    🥈 Level 2：决定方向
    MA5/10/20/60结构 + ADX趋势强度 + BOLL位置 + BIAS乖离率
    """
    结果 = {
        "方向": "震荡",
        "强度": 0,  # -100~100, 负=空头, 正=多头
        "细节": {}
    }

    # 1. MA结构
    ma5 = 最新.get('MA5', 0)
    ma10 = 最新.get('MA10', 0)
    ma20 = 最新.get('MA20', 0)
    ma60 = 最新.get('MA60', 0)

    if pd.notna(ma5) and pd.notna(ma10) and pd.notna(ma20) and pd.notna(ma60):
        if ma5 > ma10 > ma20 > ma60:
            结果["方向"] = "多头排列"
            结果["强度"] += 40
        elif ma5 < ma10 < ma20 < ma60:
            结果["方向"] = "空头排列"
            结果["强度"] -= 40
        else:
            结果["方向"] = "震荡缠绕"
    else:
        结果["方向"] = "数据不足"

    结果["细节"]["MA结构"] = 结果["方向"]

    # 2. ADX趋势强度
    adx = 最新.get('ADX', 0)
    if pd.notna(adx):
        if adx > ADX_强趋势:
            结果["细节"]["ADX"] = f"强趋势({adx:.1f})"
            结果["强度"] += 20 if 结果["强度"] >= 0 else -20
        elif adx > ADX_中等趋势:
            结果["细节"]["ADX"] = f"中等趋势({adx:.1f})"
            结果["强度"] += 10 if 结果["强度"] >= 0 else -10
        else:
            结果["细节"]["ADX"] = f"无趋势震荡({adx:.1f})"

    # 3. BOLL位置
    布林中轨 = 最新.get('布林中轨', 0)
    布林上轨 = 最新.get('布林上轨', 0)
    布林下轨 = 最新.get('布林下轨', 0)
    收盘 = 最新['close']
    if pd.notna(布林中轨) and pd.notna(布林上轨):
        if 收盘 > 布林上轨:
            结果["细节"]["BOLL"] = f"突破上轨({收盘:.2f}>{布林上轨:.2f})"
            结果["强度"] += 10
        elif 收盘 < 布林下轨:
            结果["细节"]["BOLL"] = f"跌破下轨({收盘:.2f}<{布林下轨:.2f})"
            结果["强度"] -= 10
        else:
            结果["细节"]["BOLL"] = f"轨道内({布林下轨:.2f}~{布林上轨:.2f})"

    # 4. BIAS乖离率
    bias20 = 最新.get('BIAS20', 0)
    if pd.notna(bias20):
        if bias20 > BIAS_危险追高:
            结果["细节"]["BIAS"] = f"危险追高区({bias20:+.2f}%)"
            结果["强度"] += 5  # 趋势强但追高风险大
        elif bias20 > BIAS_强趋势上:
            结果["细节"]["BIAS"] = f"强趋势区({bias20:+.2f}%)"
            结果["强度"] += 15
        elif bias20 >= BIAS_强趋势下:
            结果["细节"]["BIAS"] = f"正常区({bias20:+.2f}%)"
        else:
            结果["细节"]["BIAS"] = f"超跌区({bias20:+.2f}%)"
            结果["强度"] += 5  # 超跌反弹机会

    return 结果


def level3_节奏判断(最新: dict, 前一条: dict, 数据框: pd.DataFrame) -> dict:
    """
    🥉 Level 3：决定节奏
    MACD + 成交量结构(VMA) + 换手率 + RSI/KDJ/OBV
    """
    结果 = {
        "节奏": "中性",
        "动能方向": 0,  # -100~100
        "细节": {}
    }

    # 1. MACD
    dif = 最新.get('DIF', 0)
    dea = 最新.get('DEA', 0)
    macd柱 = 最新.get('MACD柱', 0)
    前dif = 前一条.get('DIF', 0)

    if pd.notna(dif) and pd.notna(dea):
        if dif > dea and macd柱 > 0:
            结果["细节"]["MACD"] = "多头+柱线放大"
            结果["动能方向"] += 20
        elif dif > dea and macd柱 < 0:
            结果["细节"]["MACD"] = "多头但柱线缩小(顶背离风险)"
            结果["动能方向"] += 5
        elif dif < dea and macd柱 < 0:
            结果["细节"]["MACD"] = "空头+柱线放大"
            结果["动能方向"] -= 20
        elif dif < dea and macd柱 > 0:
            结果["细节"]["MACD"] = "空头但柱线缩小(底背离机会)"
            结果["动能方向"] -= 5

    # 2. 成交量结构
    volume = 最新.get('volume', 0)
    量MA5 = 最新.get('量MA5', 0)
    量MA10 = 最新.get('量MA10', 0)
    if pd.notna(量MA5) and 量MA5 > 0:
        量比 = volume / 量MA5
        if 量比 > 2:
            结果["细节"]["成交量"] = f"放量({量比:.1f}倍量MA5)"
            结果["动能方向"] += 10
        elif 量比 > 1.2:
            结果["细节"]["成交量"] = f"温和放量({量比:.1f}倍)"
            结果["动能方向"] += 5
        elif 量比 < 0.6:
            结果["细节"]["成交量"] = f"缩量({量比:.1f}倍)"
            结果["动能方向"] -= 5
        else:
            结果["细节"]["成交量"] = f"正常({量比:.1f}倍)"

    # 3. 换手率
    换手率 = 最新.get('turnover', 0)
    if pd.notna(换手率):
        if 换手率 > 换手_活跃:
            结果["细节"]["换手率"] = f"高风险博弈({换手率:.1f}%)"
            结果["动能方向"] -= 5
        elif 换手率 > 换手_健康:
            结果["细节"]["换手率"] = f"活跃博弈({换手率:.1f}%)"
            结果["动能方向"] += 5
        elif 换手率 > 换手_低流动性:
            结果["细节"]["换手率"] = f"健康({换手率:.1f}%)"
        else:
            结果["细节"]["换手率"] = f"低流动性({换手率:.1f}%)"
            结果["动能方向"] -= 10

    # 4. RSI
    rsi14 = 最新.get('RSI14', 50)
    if pd.notna(rsi14):
        if rsi14 > 80:
            结果["细节"]["RSI"] = f"超买({rsi14:.1f})"
            结果["动能方向"] -= 10
        elif rsi14 > 60:
            结果["细节"]["RSI"] = f"偏强({rsi14:.1f})"
            结果["动能方向"] += 10
        elif rsi14 > 40:
            结果["细节"]["RSI"] = f"中性({rsi14:.1f})"
        elif rsi14 > 20:
            结果["细节"]["RSI"] = f"偏弱({rsi14:.1f})"
            结果["动能方向"] -= 10
        else:
            结果["细节"]["RSI"] = f"超卖({rsi14:.1f})"
            结果["动能方向"] += 5

    # 6. 总体节奏判定
    if 结果["动能方向"] >= 30:
        结果["节奏"] = "强势上攻"
    elif 结果["动能方向"] >= 10:
        结果["节奏"] = "震荡偏多"
    elif 结果["动能方向"] >= -10:
        结果["节奏"] = "中性震荡"
    elif 结果["动能方向"] >= -30:
        结果["节奏"] = "震荡偏空"
    else:
        结果["节奏"] = "弱势下行"

    return 结果


def 三层过滤(最新: dict, 前一条: dict, 数据框: pd.DataFrame,
            资金流向: dict, 板块数据: dict, 大盘数据: dict) -> dict:
    """
    🧭 三层优先级决策系统（核心）
    返回整合后的决策建议
    """
    l1 = level1_主力资金_板块_大盘(最新, 资金流向, 板块数据, 大盘数据)
    l2 = level2_方向判断(最新)
    l3 = level3_节奏判断(最新, 前一条, 数据框)

    return {
        "Level1": l1,
        "Level2": l2,
        "Level3": l3,
        "整合结论": {
            "允许交易": l1["允许交易"],
            "方向": l2["方向"],
            "方向强度": l2["强度"],
            "节奏": l3["节奏"],
            "动能方向": l3["动能方向"],
        }
    }


