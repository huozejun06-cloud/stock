def 计算盈亏比(当前价: float, 目标价: float, 止损价: float) -> dict:
    """
    计算盈亏比 = (目标价 - 入场价) / (入场价 - 止损价)
    """
    入场价 = 当前价

    if 入场价 <= 0 or 入场价 <= 止损价:
        return {"盈亏比": 0, "评级": "无意义", "禁止交易": True}

    收益 = 目标价 - 入场价
    风险 = 入场价 - 止损价

    if 风险 <= 0:
        return {"盈亏比": float('inf'), "评级": "无风险", "禁止交易": False}

    盈亏比 = 收益 / 风险

    if 盈亏比 >= 盈亏比_优质:
        评级 = "优质"
        禁止 = False
    elif 盈亏比 >= 盈亏比_合格:
        评级 = "合格"
        禁止 = False
    elif 盈亏比 >= 盈亏比_一般:
        评级 = "一般"
        禁止 = False
    else:
        评级 = "差"
        禁止 = True

    return {
        "盈亏比": round(盈亏比, 2),
        "入场价": round(入场价, 2),
        "目标价": round(目标价, 2),
        "止损价": round(止损价, 2),
        "预期收益": round(收益 / 入场价 * 100, 2),
        "预期风险": round(风险 / 入场价 * 100, 2),
        "评级": 评级,
        "禁止交易": 禁止,
    }


def 建议目标价与止损(最新: dict, 关键价位: dict) -> dict:
    """
    根据技术位给出目标价和止损价建议
    """
    close = 最新['close']
    atr14 = 关键价位.get('ATR14', close * 0.03)

    # 止损：关键支撑 - 0.5 ATR
    止损位 = 关键价位['支撑位1'] - atr14 * 0.5

    # 止损不能低于支撑位2（防御性读取，兼容关键价位缺少支撑位2的情况）
    if 关键价位.get('支撑位2') is not None and 止损位 < 关键价位['支撑位2']:
        止损位 = 关键价位.get('支撑位1', 止损位)

    # 第一目标：压力位1
    第一目标 = 关键价位['压力位1']

    # 第二目标：压力位2
    第二目标 = 关键价位['压力位2']

    return {
        "入场价": close,
        "止损位": round(止损位, 2),
        "第一目标": round(第一目标, 2),
        "第二目标": round(第二目标, 2),
        "建议依据": f"入场{close:.2f} | 止损{止损位:.2f}(支撑位{关键价位['支撑位1']:.2f}-ATR×0.5) | 目标{第一目标:.2f}/{第二目标:.2f}"
    }


