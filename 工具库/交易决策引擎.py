def 判断主力行为(最新: dict, 前一条: dict, 数据框: pd.DataFrame,
                 资金流向: dict) -> dict:
    """
    判定主力行为：吸筹、洗盘、拉升、出货、震荡控盘
    """
    if 数据框 is None or len(数据框) < 5:
        return {"行为": "数据不足", "置信度": 0}

    close = 最新['close']
    前close = 前一条['close']
    volume = 最新.get('volume', 0)
    前volume = 前一条.get('volume', 0)
    涨跌幅 = 最新.get('pct_chg', 0)

    try:
        主力净流入 = float(资金流向.get("主力净流入", 0))
    except (ValueError, TypeError):
        主力净流入 = 0

    量放量 = 前volume > 0 and volume > 前volume * 1.3

    # 吸筹：温和放量小阳线 + 主力净流入为正
    if (0 < 涨跌幅 < 3 and 量放量 and 主力净流入 > 0 and
            close < 最新.get('MA60', float('inf')) * 1.1):
        return {
            "行为": "吸筹",
            "置信度": 75,
            "依据": f"温和放量+小阳线+主力净流入{主力净流入/10000:.0f}万",
            "描述": "主力低位吸筹迹象"
        }

    # 洗盘：缩量下跌 + 下影线 + 未破关键均线
    if (-3 < 涨跌幅 < 0 and
            volume < 前volume * 0.8 and
            最新.get('low', close) < close * 0.97 and
            close > 最新.get('MA20', 0)):
        return {
            "行为": "洗盘",
            "置信度": 70,
            "依据": "缩量下跌+下影线+守住MA20",
            "描述": "主力洗盘特征明显"
        }

    # 拉升：放量大阳线 + 主力大单净流入
    if (涨跌幅 > 4 and 量放量 and 主力净流入 > 0):
        return {
            "行为": "拉升",
            "置信度": 85,
            "依据": f"放量大涨{涨跌幅:+.2f}%+主力净流入{主力净流入/10000:.0f}万",
            "描述": "主力拉升阶段"
        }

    # 出货：放量滞涨/放量下跌 + 主力净流出
    if ((涨跌幅 < -2 or (abs(涨跌幅) < 2 and 量放量)) and 主力净流入 < 0):
        return {
            "行为": "出货",
            "置信度": 70,
            "依据": f"放量但价格不动+主力净流出{-主力净流入/10000:.0f}万",
            "描述": "主力出货迹象"
        }

    # 震荡控盘（默认）
    return {
        "行为": "震荡控盘",
        "置信度": 60,
        "依据": f"涨跌幅{涨跌幅:+.2f}%+量能无异常",
        "描述": "主力震荡控盘"
    }


