def 一键决策(全量数据: dict, 板块数据: dict = None, 大盘数据: dict = None) -> dict:
    """
    从全量数据中执行完整决策流程，输出最终交易信号。
    支持六维评分：资金面+趋势结构+量价结构+动量系统+情绪板块+基本面
    """
    数据框 = 全量数据.get("数据框")
    最新 = 全量数据.get("最新")
    前一条 = 全量数据.get("前一条")
    资金流向 = 全量数据.get("资金流向", {})
    市场情绪 = 全量数据.get("市场情绪", {})
    量价形态 = 全量数据.get("量价形态", "未知")
    基本面 = 全量数据.get("基本面", None)

    if 数据框 is None or 最新 is None:
        return {
            "信号": 信号_D,
            "信号名称": "禁止交易",
            "仓位": (0, 0),
            "理由": ["数据不足，无法决策"],
            "一句话结论": "🔴 数据不足，禁止交易",
        }

    # 默认大盘数据
    if 大盘数据 is None:
        大盘数据 = {
            "上证涨跌": 全量数据.get("上证涨跌", 0),
            "深证涨跌": 全量数据.get("深证涨跌", 0),
            "创业板涨跌": 全量数据.get("创业板涨跌", 0),
        }

    # Step 1: 市场状态压缩
    市场状态 = 判断市场状态(数据框, 最新)

    # Step 2: 三层过滤
    三层过滤结果 = 三层过滤(最新, 前一条, 数据框, 资金流向, 板块数据, 大盘数据)

    # Step 3: 量化评分
    评分结果 = 量化评分(最新, 资金流向, 板块数据, 市场情绪, 量价形态, 基本面)

    # Step 4: 三大过滤器
    BIAS结果 = BIAS过滤器(最新)
    背离结果 = 背离检测(最新, 前一条, 数据框)
    换手结果 = 换手率过滤器(最新)

    # Step 5: 量价结构
    量价结构结果 = 判断量价结构(最新, 前一条)

    # Step 6: 主力行为
    主力行为 = 判断主力行为(最新, 前一条, 数据框, 资金流向)

    # Step 7: 关键价位
    关键价位 = 计算关键价位(最新, 数据框)

    # Step 8: 盈亏比
    目标止损 = 建议目标价与止损(最新, 关键价位)
    盈亏比结果 = 计算盈亏比(
        目标止损["入场价"],
        目标止损["第一目标"],
        目标止损["止损位"]
    )

    # Step 9: 风控红线
    红线结果 = 风控红线检查(
        最新, 前一条, 数据框, 资金流向,
        板块数据, 背离结果, BIAS结果, 量价结构结果
    )

    # Step 10: 最终决策
    决策结果 = 最终决策(
        评分结果, 红线结果, 三层过滤结果,
        盈亏比结果, BIAS结果, 背离结果, 换手结果,
        量价结构结果, 主力行为, 关键价位, 市场状态
    )

    # Step 11: 规则19 - 分时W底确认
    try:
        股票代码_w = 全量数据.get("股票代码", "")
        if 股票代码_w:
            w_result = rule_19_w_bottom_confirm(股票代码_w)
            if w_result["生效"] and w_result["调整幅度"] > 0:
                调整幅度 = w_result["调整幅度"]
                原总分 = 决策结果.get("评分", {}).get("总分", 0)
                新总分 = 原总分 * (1 + 调整幅度)
                决策结果["评分"]["总分"] = min(100, round(新总分, 1))
                # 更新评级
                if 决策结果["评分"]["总分"] >= 85:
                    决策结果["评分"]["评级"] = "A-优秀"
                elif 决策结果["评分"]["总分"] >= 70:
                    决策结果["评分"]["评级"] = "B-良好"
                elif 决策结果["评分"]["总分"] >= 50:
                    决策结果["评分"]["评级"] = "C-及格"
                else:
                    决策结果["评分"]["评级"] = "D-不及格"

                描述 = w_result.get("W底详情", {}).get("description", "")
                决策结果["理由"].append(f"规则19 W底确认✅：调整幅度+{调整幅度*100:.0f}%，{描述}")
    except Exception as e:
        pass  # W底识别失败不影响主流程

    # 组装交易执行模板
    交易模板 = {
        "入场区间": f"{最新['close']:.2f} ~ {目标止损['第一目标']:.2f}",
        "止损位": f"{目标止损['止损位']:.2f}",
        "第一目标": f"{目标止损['第一目标']:.2f}",
        "第二目标": f"{目标止损['第二目标']:.2f}",
        "仓位": f"{决策结果['仓位'][0]}%-{决策结果['仓位'][1]}%",
        "胜率评估": "高" if 评分结果["总分"] >= 80 else ("中" if 评分结果["总分"] >= 60 else "低"),
        "盈亏比": round(盈亏比结果["盈亏比"], 2),
    }

    决策结果["交易模板"] = 交易模板
    决策结果["目标止损"] = 目标止损

    return 决策结果


