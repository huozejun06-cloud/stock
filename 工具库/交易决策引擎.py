def 计算关键价位(最新: dict, 数据框: pd.DataFrame) -> dict:
    """
    计算关键价位：支撑位1/2、压力位1/2、多空分界线
    """
    if 数据框 is None or len(数据框) < 20:
        return {"支撑位1": 0, "支撑位2": 0, "压力位1": 0, "压力位2": 0, "多空分界线": 0}

    close = 最新['close']
    布林中轨 = 最新.get('布林中轨', 0)
    布林上轨 = 最新.get('布林上轨', 0)
    布林下轨 = 最新.get('布林下轨', 0)
    ma20 = 最新.get('MA20', 0)
    ma60 = 最新.get('MA60', 0)
    atr14 = 最新.get('ATR14', close * 0.03)

    if pd.isna(atr14) or atr14 <= 0:
        atr14 = close * 0.03

    # 多空分界线 = MA20（最核心）
    多空分界线 = ma20 if pd.notna(ma20) else close

    # 支撑位
    支撑位1 = 布林中轨 if pd.notna(布林中轨) else close * 0.97
    支撑位2 = 布林下轨 if pd.notna(布林下轨) else close * 0.95

    # 压力位
    压力位1 = 布林上轨 if pd.notna(布林上轨) else close * 1.03
    压力位2 = close + atr14 * 3

    # 确保支撑 < 压力
    if 支撑位1 >= 压力位1:
        支撑位1 = close * 0.97
        压力位1 = close * 1.03

    return {
        "支撑位1": round(支撑位1, 2),
        "支撑位2": round(支撑位2, 2),
        "压力位1": round(压力位1, 2),
        "压力位2": round(压力位2, 2),
        "多空分界线": round(多空分界线, 2),
        "ATR14": round(atr14, 2),
        "描述": f"多空分界线{多空分界线:.2f}(MA20) | 支撑{支撑位1:.2f}/{支撑位2:.2f} | 压力{压力位1:.2f}/{压力位2:.2f}"
    }


