def 风控红线检查(最新: dict, 前一条: dict, 数据框: pd.DataFrame,
                资金流向: dict, 板块数据: dict, 背离结果: dict,
                BIAS结果: dict, 量价结构结果: dict) -> dict:
    """
    ⛔ 风控红线系统（强制拦截）
    任一触发 → 禁止交易
    """
    红线结果 = {
        "触发": False,
        "触发红线": [],
        "描述": "",
        "禁止交易": False,
    }

    # 红线1：重度背离
    if 背离结果.get("禁止交易", False):
        红线结果["触发"] = True
        红线结果["触发红线"].append("重度背离")
        红线结果["禁止交易"] = True

    # 红线2：主力资金连续流出
    try:
        主力净流入 = float(资金流向.get("主力净流入", 0))
    except (ValueError, TypeError):
        主力净流入 = 0
    if 主力净流入 < -50000000:  # 主力流出>5000万
        红线结果["触发"] = True
        红线结果["触发红线"].append(f"主力资金连续流出({主力净流入/10000:.0f}万)")

    # 红线3：板块退潮
    if 板块数据:
        板块涨跌幅 = 板块数据.get('涨跌幅', 0)
        if 板块涨跌幅 < -3:
            红线结果["触发"] = True
            红线结果["触发红线"].append(f"板块退潮({板块涨跌幅:+.2f}%)")

    # 红线4：BIAS过热 + 放量滞涨
    if (BIAS结果.get("状态") == "危险追高区" and
            量价结构结果.get("类型") in ["放量滞涨", "无量上涨"]):
        红线结果["触发"] = True
        红线结果["触发红线"].append(f"BIAS过热({BIAS结果['值']:+.1f}%)+放量滞涨")

    # 红线5：跌破MA20放量
    close = 最新['close']
    ma20 = 最新.get('MA20', 0)
    volume = 最新.get('volume', 0)
    量MA5 = 最新.get('量MA5', 0)

    if (pd.notna(ma20) and close < ma20 and
            pd.notna(量MA5) and 量MA5 > 0 and volume > 量MA5 * 1.2):
        红线结果["触发"] = True
        红线结果["触发红线"].append(f"跌破MA20({ma20:.2f})放量")

    if 红线结果["触发"]:
        红线结果["禁止交易"] = True
        红线结果["描述"] = "⛔ 触发风控红线: " + " + ".join(红线结果["触发红线"])
    else:
        红线结果["描述"] = "✅ 风控红线全部通过"

    return 红线结果


