def 背离检测(最新: dict, 前一条: dict, 数据框: pd.DataFrame) -> dict:
    """
    背离检测：MACD/RSI/OBV
    返回：无/轻度/中度/重度
    """
    结果 = {
        "MACD背离": {"级别": "无", "描述": ""},
        "RSI背离": {"级别": "无", "描述": ""},
        "OBV背离": {"级别": "无", "描述": ""},
        "最大级别": "无",
        "禁止交易": False,
    }

    if 数据框 is None or len(数据框) < 5:
        return 结果

    close = 最新['close']
    前close = 前一条['close']
    dif = 最新.get('DIF', 0)
    前dif = 前一条.get('DIF', 0)
    rsi14 = 最新.get('RSI14', 50)
    前rsi14 = 前一条.get('RSI14', 50)
    obv = 最新.get('OBV', 0)
    前obv = 前一条.get('OBV', 0)

    # MACD顶背离：价格创新高，DIF未创新高
    if pd.notna(dif) and pd.notna(前dif) and len(数据框) > 10:
        高价 = 数据框['close'].tail(10).max()
        高dif = 数据框['DIF'].tail(10).max() if 'DIF' in 数据框.columns else dif
        高价位置 = 数据框['close'].tail(10).idxmax()
        高dif位置 = 数据框['DIF'].tail(10).idxmax() if 'DIF' in 数据框.columns else None

        if 高dif位置 and 高价位置 and 高价位置 > 高dif位置 and close >= 高价 * 0.99:
            if dif < 高dif * 0.95:
                结果["MACD背离"] = {"级别": "中度", "描述": "价格接近新高但DIF未同步，顶背离迹象"}
            else:
                结果["MACD背离"] = {"级别": "轻度", "描述": "价格与DIF略有背离"}

    # RSI背离
    if pd.notna(rsi14) and pd.notna(前rsi14):
        高价rsi = 数据框['RSI14'].tail(10).max() if 'RSI14' in 数据框.columns else rsi14
        高价close = 数据框['close'].tail(10).max()

        if close >= 高价close * 0.99 and rsi14 < 高价rsi * 0.9:
            结果["RSI背离"] = {"级别": "中度", "描述": f"RSI顶背离(价高{close:.2f}但RSI{rsi14:.1f}<{高价rsi:.1f})"}
        elif close >= 高价close * 0.99 and rsi14 < 高价rsi:
            结果["RSI背离"] = {"级别": "轻度", "描述": "RSI略有背离"}

    # OBV背离
    if pd.notna(obv) and pd.notna(前obv) and len(数据框) > 10:
        高价obv = 数据框['OBV'].tail(10).max() if 'OBV' in 数据框.columns else obv
        高价close = 数据框['close'].tail(10).max()

        if close >= 高价close * 0.99 and obv < 高价obv * 0.95:
            结果["OBV背离"] = {"级别": "中度", "描述": f"OBV顶背离(量价不配合)"}
        elif close >= 高价close * 0.99 and obv < 高价obv:
            结果["OBV背离"] = {"级别": "轻度", "描述": "OBV略有背离"}

    # 判断最大级别
    级别优先级 = {"无": 0, "轻度": 1, "中度": 2, "重度": 3}
    最大 = "无"
    for k in ["MACD背离", "RSI背离", "OBV背离"]:
        if 级别优先级.get(结果[k]["级别"], 0) > 级别优先级[最大]:
            最大 = 结果[k]["级别"]
    结果["最大级别"] = 最大

    if 最大 == "重度":
        结果["禁止交易"] = True

    return 结果


