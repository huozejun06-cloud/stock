def 量化评分(最新: dict, 资金流向: dict, 板块数据: dict,
            市场情绪: dict, 量价形态: str, 基本面: dict = None) -> dict:
    """
    📊 指标量化评分系统（强制）
    每个维度0-100分，加权计算总分
    资金面30% + 趋势结构25% + 量价结构20% + 动量系统15% + 情绪/板块10%
    """
    评分结果 = {}

    # 资金面评分（权重30%）
    主力值 = 资金流向.get('主力净流入')
    if 主力值 is None or str(主力值).strip() in ('N/A', 'None', '') or 资金流向.get('数据有效') == False:
        资金面得分 = 0
        资金状态 = "⏸ 非交易时段(跳过)"
    else:
        资金面得分 = 计算资金面分数(资金流向)
        资金状态 = "✅ 实时数据"
    
    资金分 = 资金面得分
    净流入 = 资金流向.get("主力净流入", 0) or 0
    评分结果["资金面"] = {"得分": 资金分, "权重": 权重_资金面, "依据": f"{资金状态} 主力净流入{float(净流入)/10000:.0f}万"}


    # --- 2. 趋势结构评分 (25%) ---
    ma5 = 最新.get('MA5', 0)
    ma10 = 最新.get('MA10', 0)
    ma20 = 最新.get('MA20', 0)
    ma60 = 最新.get('MA60', 0)
    adx = 最新.get('ADX', 0)
    bias20 = 最新.get('BIAS20', 0)

    趋势分 = 50  # 基准分

    # 均线结构
    if pd.notna(ma5) and pd.notna(ma10) and pd.notna(ma20) and pd.notna(ma60):
        if ma5 > ma10 > ma20 > ma60:
            趋势分 += 30
        elif ma5 > ma10 > ma20:
            趋势分 += 15
        elif ma5 < ma10 < ma20 < ma60:
            趋势分 -= 20
        elif ma5 < ma10 < ma20:
            趋势分 -= 10

    # ADX趋势强度
    if pd.notna(adx):
        if adx > ADX_强趋势:
            趋势分 += 15 if 趋势分 >= 50 else -15
        elif adx > ADX_中等趋势:
            趋势分 += 10 if 趋势分 >= 50 else -10

    # BIAS位置
    if pd.notna(bias20):
        if BIAS_强趋势上 < bias20 < BIAS_危险追高:
            趋势分 += 10  # 强势区
        elif BIAS_强趋势下 < bias20 < 0:
            趋势分 -= 5  # 弱势区
        elif bias20 < BIAS_超跌:
            趋势分 += 5  # 超跌反弹机会
        elif bias20 > BIAS_危险追高:
            趋势分 -= 10  # 追高风险

    趋势分 = max(0, min(100, 趋势分))
    评分结果["趋势结构"] = {"得分": 趋势分, "权重": 权重_趋势结构, "依据": f"MA排列+ADX={adx:.1f}+BIAS={bias20:+.1f}%"}

    # --- 3. 量价结构评分 (20%) ---
    量价分 = 50
    volume = 最新.get('volume', 0)
    量MA5 = 最新.get('量MA5', 0)
    前涨跌幅 = 最新.get('pct_chg', 0)

    if 量价形态 == "放量上涨":
        量价分 = 85
    elif 量价形态 == "放量下跌":
        量价分 = 20
        量价分 = 15  # 🔥 放量下跌是最危险的信号之一
    elif 量价形态 == "缩量上涨":
        量价分 = 60  # 缩量上涨可能是诱多
    elif 量价形态 == "缩量下跌":
        量价分 = 40  # 缩量下跌可能是洗盘

    # 量比修正
    if pd.notna(量MA5) and 量MA5 > 0 and volume > 0:
        量比 = volume / 量MA5
        if 量比 > 3 and 前涨跌幅 < 3:
            量价分 -= 20  # 放量滞涨
        elif 量比 > 2 and 前涨跌幅 > 3:
            量价分 += 10  # 放量上涨加分

    # 换手率修正
    换手率 = 最新.get('turnover', 0)
    if pd.notna(换手率):
        if 换手率 > 换手_活跃:
            量价分 -= 10
        elif 换手率 < 换手_低流动性:
            量价分 -= 15

    量价分 = max(0, min(100, 量价分))
    评分结果["量价结构"] = {"得分": 量价分, "权重": 权重_量价结构, "依据": f"形态: {量价形态}, 换手: {换手率:.1f}%"}

    # --- 4. 动量系统评分 (15%) ---
    动量分 = 50
    dif = 最新.get('DIF', 0)
    dea = 最新.get('DEA', 0)
    macd柱 = 最新.get('MACD柱', 0)
    rsi14 = 最新.get('RSI14', 50)

    # MACD
    if pd.notna(dif) and pd.notna(dea):
        if dif > dea and macd柱 > 0:
            动量分 += 20
        elif dif > dea and macd柱 < 0:
            动量分 += 5  # 多头但动能减弱
        elif dif < dea and macd柱 > 0:
            动量分 -= 5  # 空头但有底背离迹象
        elif dif < dea and macd柱 < 0:
            动量分 -= 20

    # RSI
    if pd.notna(rsi14):
        if 60 < rsi14 < 80:
            动量分 += 10
        elif rsi14 > 80:
            动量分 -= 5  # 超买
        elif 40 < rsi14 < 60:
            动量分 += 0
        elif 20 < rsi14 < 40:
            动量分 -= 10
        else:
            动量分 += 5  # 超卖反弹机会



    动量分 = max(0, min(100, 动量分))
    评分结果["动量系统"] = {"得分": 动量分, "权重": 权重_动量系统, "依据": f"MACD+RSI{rsi14:.1f}"}

    # --- 5. 情绪/板块评分 (10%) ---
    情绪分 = 50

    # 涨停家数
    try:
        涨停 = int(市场情绪.get('涨停家数', 0))
    except (ValueError, TypeError):
        涨停 = 0
    if 涨停 >= 80:
        情绪分 += 30
    elif 涨停 >= 30:
        情绪分 += 10
    elif 涨停 >= 10:
        情绪分 -= 10
    else:
        情绪分 -= 20

    # 板块强度
    if 板块数据:
        板块涨跌幅 = 板块数据.get('涨跌幅', 0)
        if 板块涨跌幅 > 3:
            情绪分 += 15
        elif 板块涨跌幅 > 1:
            情绪分 += 5
        elif 板块涨跌幅 < -2:
            情绪分 -= 15

    # 市场情绪评级
    情绪评级 = 市场情绪.get('情绪评级', '中性')
    if 情绪评级 == '活跃':
        情绪分 += 5
    elif 情绪评级 == '退潮':
        情绪分 -= 10

    情绪分 = max(0, min(100, 情绪分))
    评分结果["情绪/板块"] = {"得分": 情绪分, "权重": 权重_情绪板块, "依据": f"涨停{涨停}家, 情绪:{情绪评级}"}

    # --- 计算总分 ---
    # 非交易时段：资金面跳过，权重重分配给趋势结构
    _资金权重 = 0 if 资金状态.startswith("⏸") else 权重_资金面
    _趋势权重 = 权重_趋势结构 + (权重_资金面 if 资金状态.startswith("⏸") else 0)
    总分 = (
        资金分 * _资金权重 +
        趋势分 * _趋势权重 +
        量价分 * 权重_量价结构 +
        动量分 * 权重_动量系统 +
        情绪分 * 权重_情绪板块
    )

    评分结果["总分"] = 总分

    # 评级
    if 总分 >= 总分_强做多:
        评级 = "强做多"
    elif 总分 >= 总分_可交易:
        评级 = "可交易"
    elif 总分 >= 总分_观望:
        评级 = "观望"
    else:
        评级 = "禁止交易"

    评分结果["评级"] = 评级

    return 评分结果


