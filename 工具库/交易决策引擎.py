def 最终决策(评分结果: dict, 红线结果: dict, 三层过滤结果: dict,
            盈亏比结果: dict, BIAS结果: dict, 背离结果: dict,
            换手结果: dict, 量价结构结果: dict, 主力行为: dict,
            关键价位: dict, 市场状态: dict) -> dict:
    """
    🚀 最终交易信号输出
    只输出四种之一：A(强烈做多) / B(轻仓试探) / C(观望) / D(禁止交易)
    """
    信号 = 信号_D  # 默认禁止交易
    仓位 = (0, 0)
    理由 = []

    # === 一票否决：风控红线触发 ===
    if 红线结果["禁止交易"]:
        return {
            "信号": 信号_D,
            "信号名称": "禁止交易",
            "仓位": (0, 0),
            "理由": [红线结果["描述"]],
            "评分": 评分结果,
            "细节": {"入场条件": "不适用", "失效条件": "不适用"},
            "一句话结论": "⛔ 触发风控红线，禁止交易",
        }

    # === 一票否决：重度背离 ===
    if 背离结果.get("禁止交易", False):
        return {
            "信号": 信号_D,
            "信号名称": "禁止交易",
            "仓位": (0, 0),
            "理由": [f"重度背离({背离结果['最大级别']})，禁止交易"],
            "评分": 评分结果,
            "细节": {"入场条件": "不适用", "失效条件": "不适用"},
            "一句话结论": "⛔ 重度背离，禁止交易",
        }

    # === 一票否决：Level 1 禁止交易 ===
    if not 三层过滤结果["整合结论"]["允许交易"]:
        return {
            "信号": 信号_D,
            "信号名称": "禁止交易",
            "仓位": (0, 0),
            "理由": 三层过滤结果["Level1"]["警报"],
            "评分": 评分结果,
            "细节": {"入场条件": "不适用", "失效条件": "不适用"},
            "一句话结论": "⛔ Level 1 风险过高，禁止交易",
        }

    # === 根据评分决定信号 ===
    总分 = 评分结果["总分"]
    评分评级 = 评分结果["评级"]

    if 总分 >= 85:
        # A：强烈做多
        信号 = 信号_A
        信号名称 = "强烈做多"
        仓位 = 仓位_A_强做多
        理由.append(f"总分{总分:.1f}≥85，强做多信号")
    elif 总分 >= 70:
        # 判断是否B还是C
        if (三层过滤结果["整合结论"]["方向强度"] > 0 and
                三层过滤结果["整合结论"]["动能方向"] > 0 and
                BIAS结果["结论"] != "警告" and
                换手结果["结论"] != "警告"):
            信号 = 信号_B
            信号名称 = "轻仓试探"
            仓位 = 仓位_B_轻仓
            理由.append(f"总分{总分:.1f}≥70，但需轻仓试探")
        else:
            信号 = 信号_C
            信号名称 = "观望"
            仓位 = 仓位_C_观望
            理由.append(f"总分{总分:.1f}可交易区间，但辅助指标有疑虑")
    elif 总分 >= 50:
        信号 = 信号_C
        信号名称 = "观望"
        仓位 = 仓位_C_观望
        理由.append(f"总分{总分:.1f}，观望等待")
    else:
        信号 = 信号_D
        信号名称 = "禁止交易"
        仓位 = 仓位_D_禁止
        理由.append(f"总分{总分:.1f}<50，评分过低")

    # 补充理由
    if BIAS结果.get("结论") == "警告":
        理由.append(f"⚠ {BIAS结果['描述']}")
    if 换手结果.get("结论") == "警告":
        理由.append(f"⚠ {换手结果['描述']}")
    if 量价结构结果.get("风险") == "高":
        理由.append(f"⚠ 量价结构风险: {量价结构结果['类型']}")
    if 盈亏比结果["禁止交易"]:
        理由.append(f"⚠ 盈亏比{盈亏比结果['盈亏比']}<2，不满足要求")

    # 添加盈亏比信息
    if not 盈亏比结果["禁止交易"]:
        理由.append(f"📊 盈亏比={盈亏比结果['盈亏比']}({盈亏比结果['评级']})")

    # 一句话结论
    if 信号 == 信号_A:
        一句话 = f"🟢 {关键价位['描述'].split('|')[0] if 关键价位 else ''} | 总分{总分:.0f} 强烈做多"
    elif 信号 == 信号_B:
        一句话 = f"🟡 轻仓试探 | 总分{总分:.0f} 盈亏比{盈亏比结果['盈亏比']}"
    elif 信号 == 信号_C:
        一句话 = f"🟠 观望等待 | 总分{总分:.0f} 市场状态:{市场状态.get('状态','未知')}"
    else:
        一句话 = f"🔴 禁止交易 | 总分{总分:.0f} | 风险过高"

    return {
        "信号": 信号,
        "信号名称": 信号名称,
        "仓位": 仓位,
        "理由": 理由,
        "评分": 评分结果,
        "市场状态": 市场状态,
        "三层过滤": 三层过滤结果,
        "BIAS": BIAS结果,
        "背离": 背离结果,
        "换手": 换手结果,
        "量价结构": 量价结构结果,
        "主力行为": 主力行为,
        "关键价位": 关键价位,
        "盈亏比": 盈亏比结果,
        "一句话结论": 一句话,
        "入场条件": "开盘后30分钟内站稳开盘价上方" if 信号 in [信号_A, 信号_B] else "不适用",
        "失效条件": "跌破入场价-ATR×0.5或盘中放量下跌" if 信号 in [信号_A, 信号_B] else "不适用",
    }


