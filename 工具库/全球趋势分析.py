def 分析美股对A股影响(美股数据: dict) -> dict:
    """分析美国科技股走势对A股对应板块的影响"""
    if not 美股数据: return {"影响":"数据不足","板块建议":[]}
    影响结果 = {"整体影响":"neutral","板块建议":[],"热点传导":""}
    看多板块 = []; 看空板块 = []; 涨跌明细 = []
    
    for 板块名, 映射 in 美股到A股映射.items():
        走势 = []
        for 美股代码 in 映射["美股"]:
            for 股 in 美股数据:
                if 股.get("代码","").upper() == 美股代码:
                    d = 股.get("涨跌幅") or 0
                    走势.append(d)
                    涨跌明细.append(f"{板块名}/{美股代码}: {d:+.2f}%")
        if not 走势: continue
        平均涨跌 = sum(走势) / len(走势)
        if 平均涨跌 > 0.5 and 映射["A股板块"]:
            看多板块.append({"板块":映射["A股板块"],"置信度":round(映射["影响因子"]*min(abs(平均涨跌)/2,1),2)})
        elif 平均涨跌 < -0.5 and 映射["A股板块"]:
            看空板块.append({"板块":映射["A股板块"],"置信度":round(映射["影响因子"]*min(abs(平均涨跌)/2,1),2)})
    
    影响结果["板块建议"] = {"看多":看多板块,"看空":看空板块}
    影响结果["涨跌明细"] = "\n".join(涨跌明细) if 涨跌明细 else "无数据"
    
    if 看多板块 and not 看空板块:
        影响结果["整体影响"] = "positive"
        影响结果["热点传导"] = f"美股科技板块整体走强，利好A股{','.join(p['板块'] for p in 看多板块[:3])}"
    elif 看空板块 and not 看多板块:
        影响结果["整体影响"] = "negative"
        影响结果["热点传导"] = f"美股科技板块回调，A股{','.join(p['板块'] for p in 看空板块[:3])}可能承压"
    else:
        影响结果["热点传导"] = "美股板块分化，需结合A股自身走势判断"
    
    return 影响结果

def 分析亚太对A股影响(全球指数: list) -> dict:
    """分析日韩港市场对A股情绪的影响"""
    if not 全球指数: return {"影响":"数据不足"}
    东亚指数 = [i for i in 全球指数 if i.get("市场") in ["日本","韩国","香港"]]
    日本 = [i for i in 东亚指数 if i.get("市场")=="日本"]
    韩国 = [i for i in 东亚指数 if i.get("市场")=="韩国"]
    香港 = [i for i in 东亚指数 if i.get("市场")=="香港"]
    lines = []
    if 日本: lines.append(f"日本{'走强' if 日本[0].get('涨跌幅',0)>0 else '走弱'}({日本[0].get('涨跌幅',0):+.2f}%)")
    if 韩国: lines.append(f"韩国{'走强' if 韩国[0].get('涨跌幅',0)>0 else '走弱'}({韩国[0].get('涨跌幅',0):+.2f}%)")
    if 香港: lines.append(f"港股{'走强' if 香港[0].get('涨跌幅',0)>0 else '走弱'}({香港[0].get('涨跌幅',0):+.2f}%)")
    return {"亚太趋势":"\n".join(lines) if lines else "数据不足","东亚联动":"与A股情绪正相关"}

def 分析全球资金流向(全球数据: dict) -> dict:
    """分析全球避险/风险偏好"""
    商品 = 全球数据.get("大宗商品",[])
    黄金 = next((c for c in 商品 if "黄金" in c.get("名称","")),None)
    原油 = next((c for c in 商品 if "原油" in c.get("名称","")),None)
    比特币 = next((c for c in 商品 if "比特币" in c.get("名称","")),None)
    恐慌指数 = 全球数据.get("全球指数",[])
    vix = next((i for i in 恐慌指数 if "恐慌" in i.get("名称","")),None)
    
    items = []
    if 黄金: items.append(f"黄金{'涨' if (黄金.get('涨跌幅') or 0)>0 else '跌'}{黄金.get('涨跌幅',0):+.1f}%")
    if 原油: items.append(f"原油{'涨' if (原油.get('涨跌幅') or 0)>0 else '跌'}{原油.get('涨跌幅',0):+.1f}%")
    if 比特币: items.append(f"比特币{'涨' if (比特币.get('涨跌幅') or 0)>0 else '跌'}{比特币.get('涨跌幅',0):+.1f}%")
    if vix: items.append(f"恐慌指数{vix.get('当前价','N/A')}")
    
    风险偏好 = "risk_on" if (黄金 and (黄金.get("涨跌幅") or 0) < 0) or (比特币 and (比特币.get("涨跌幅") or 0) > 0) else "risk_off"
    return {"资金信号":"; ".join(items),"风险偏好":风险偏好}

def 生成全球趋势报告(全球数据: dict) -> str:
    """生成完整的全球趋势分析报告（供CLI/GUI使用）"""
    趋势 = 全球数据.get("全球市场趋势",{})
    指数 = 全球数据.get("全球指数",[])
    美股 = 全球数据.get("核心美股",[])
    美股影响 = 分析美股对A股影响(美股)
    亚太影响 = 分析亚太对A股影响(指数)
    
    lines = ["="*60,"🌐 全球趋势分析报告",f"获取时间: {全球数据.get('获取时间','')}","="*60]
    lines.append(f"\n📊 全球市场趋势: {趋势.get('描述','N/A')}")
    lines.append(f"\n🔗 美股→A股传导: {美股影响.get('热点传导','N/A')}")
    lines.append(f"🌏 亚太市场: {亚太影响.get('亚太趋势','N/A')}")
    lines.append(f"\n📋 A股板块建议:")
    for 方向 in ["看多","看空"]:
        for p in 美股影响.get("板块建议",{}).get(方向,[]):
            lines.append(f"  {'✅' if 方向=='看多' else '❌'} {p['板块']} (置信度{p['置信度']:.0%})")
    return "\n".join(lines)
