# 规则2：分时极限低吸信号
# ==============================================================================
def 分时低吸评分(开盘价: float, 最低价: float, 收盘价: float, 振幅: float) -> dict:
    """判断当日是否出现分时极限低吸机会
    原理：开盘后砸出的深坑（最低价远离开盘价），收盘价明显回升
    """
    if 开盘价 <= 0: return {"低吸机会": False, "评分": 0}

    开盘到最低跌幅 = (最低价 - 开盘价) / 开盘价 * 100
    收盘回升幅度 = (收盘价 - 最低价) / 最低价 * 100

    评分 = 0
    信号明细 = []

    # 盘中砸出-2%以上的坑
    if 开盘到最低跌幅 <= -2:
        评分 += 40
        信号明细.append(f"盘中深砸{开盘到最低跌幅:.1f}%")
    elif 开盘到最低跌幅 <= -1:
        评分 += 20
        信号明细.append(f"盘中回调{开盘到最低跌幅:.1f}%")

    # 从最低点有明显回升（W底雏形）
    if 收盘回升幅度 >= 3:
        评分 += 30
        信号明细.append(f"深V反弹{收盘回升幅度:.1f}%")
    elif 收盘回升幅度 >= 1.5:
        评分 += 15
        信号明细.append(f"弱V反弹{收盘回升幅度:.1f}%")

    # 收盘价站上开盘价（分时黄线上方）
    if 收盘价 >= 开盘价:
        评分 += 30
        信号明细.append("收盘站上开盘价(黄线上方)")

    return {
        "低吸机会": 评分 >= 50,
        "评分": 评分,
        "评级": "黄金坑" if 评分 >= 80 else ("优质低吸" if 评分 >= 60 else ("可关注" if 评分 >= 40 else "一般")),
        "信号明细": "; ".join(信号明细) if 信号明细 else "无明显低吸信号",
    }

