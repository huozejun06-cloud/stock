def 实战融合分析(股票代码: str, 股票名称: str, 决策引擎结果: dict = None,
                 实时数据: dict = None) -> dict:
    """
    主分析函数：融合用户实战风格的个股诊断
    输入：
    - 股票代码/名称
    - 决策引擎结果（可选，已有则直接使用）
    - 实时数据（可选，含开盘/最低/收盘/振幅等）
    """
    结果 = {
        "股票": f"{股票代码} {股票名称}",
        "分析时间": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
    }

    # 1. 核心主线判断
    主线结果 = 检查核心主线(股票代码, 股票名称)
    结果["主线诊断"] = {
        "在核心主线": 主线结果["在主线"],
        "主线名称": 主线结果["主线名"],
        "赛道": 主线结果["赛道"],
        "策略提示": "✅ 符合主线选股逻辑，可重点关注" if 主线结果["在主线"]
                    else "⚠️ 不在当前热点主线上，需确认是否有独立驱动逻辑"
    }

    # 2. 分时低吸评分（如果有实时数据）
    if 实时数据:
        低吸结果 = 分时低吸评分(
            实时数据.get("开盘价", 0),
            实时数据.get("最低价", 0),
            实时数据.get("收盘价", 0),
            实时数据.get("振幅", 0),
        )
        结果["分时低吸分析"] = 低吸结果

    # 3. 对接现有决策引擎
    if 决策引擎结果:
        信号 = 决策引擎结果.get("信号", "N/A")
        信号名 = 决策引擎结果.get("信号名称", "N/A")
        总分 = 决策引擎结果.get("评分", {}).get("总分", 0)
        仓位 = 决策引擎结果.get("仓位", (0, 0))
        一句话 = 决策引擎结果.get("一句话结论", "")

        结果["引擎信号"] = {
            "信号": 信号,
            "信号名称": 信号名,
            "总分": 总分,
            "仓位": f"{仓位[0]}%-{仓位[1]}%",
            "一句话结论": 一句话,
        }
        结果["策略融合结论"] = _融合结论(结果)

    return 结果


def _融合结论(结果: dict) -> dict:
    """生成融合用户策略的最终结论"""
    主线 = 结果.get("主线诊断", {})
    引擎 = 结果.get("引擎信号", {})

    # 核心判断逻辑
    可操作 = False
    理由 = []

    # 条件1：在主线内
    if 主线.get("在核心主线"):
        可操作 = True
        理由.append(f"在核心主线[{主线.get('主线名称')}]内")

    # 条件2：引擎信号非D
    if 引擎.get("信号") in ["A", "B"]:
        可操作 = True
        理由.append(f"引擎信号{引擎.get('信号')}({引擎.get('信号名称')})")
    elif 引擎.get("信号") == "D":
        可操作 = False
        理由.append(f"引擎禁止交易(D级)")

    # 条件3：有分时低吸机会
    低吸 = 结果.get("分时低吸分析", {})
    if 低吸.get("低吸机会"):
        可操作 = True
        理由.append(f"分时出现{低吸.get('评级')}机会")

    最终结论 = "✅ 符合你的操盘体系，可纳入观察/执行" if 可操作 else "⏸ 条件不足，等待更佳时机"

    return {
        "可操作": 可操作,
        "结论": 最终结论,
        "依据": "; ".join(理由) if 理由 else "暂无强信号",
        "操作建议": _操作建议(可操作, 引擎, 低吸),
    }


def _操作建议(可操作: bool, 引擎: dict, 低吸: dict) -> str:
    """生成符合用户风格的操作建议"""
    if not 可操作:
        return "保持观望，等待下次分时低吸机会或主线轮动到该股"

    信号 = 引擎.get("信号", "C")
    低吸评分 = 低吸.get("评分", 0)

    if 信号 == "A" and 低吸评分 >= 60:
        return "🟢 强烈建仓信号 | 策略：分时砸出深坑时果断低吸，不追高 | 仓位：70-100% | T+1锁仓，不因盘中波动恐慌"
    elif 信号 == "B" and 低吸评分 >= 40:
        return "🟡 轻仓试探信号 | 策略：等待分时回踩均线或砸盘时买入 | 仓位：10-30% | 严格止损-ATR×0.5"
    elif 信号 == "A":
        return "🟢 引擎看多但无低吸机会 | 策略：不追高，等分时回调再入场 | 仓位：10-30%"
    else:
        return "🟠 条件不成熟 | 策略：按兵不动，保证现金为王"


