# ===== 风险控制 =====
class 风控:
    """风险控制系统
    规则：
    - 单笔亏损 ≤3%
    - 单日亏损 ≤5%
    - 总回撤 ≤10%
    - 连亏3笔停止交易
    """
    def __init__(self, 初始资金=10000):
        self.初始资金 = 初始资金
        self.峰值 = 初始资金
        self.交易历史 = []
        self.连亏次数 = 0
        self.今日盈亏 = 0
    
    def 检查(self, 预期亏损: float) -> dict:
        """检查是否能交易"""
        reasons = []
        if 预期亏损 > 0.03:
            reasons.append(f'单笔亏损{预期亏损:.1%}>3%')
        if self.连亏次数 >= 3:
            reasons.append(f'连亏{self.连亏次数}笔，停止交易')
        if (self.峰值 - (self.峰值 - self.今日盈亏)) / self.峰值 > 0.10:
            reasons.append('总回撤>10%')
        
        return {'允许': len(reasons)==0, '原因': '; '.join(reasons) if reasons else '通过'}
    
    def 记录交易(self, 盈亏: float):
        self.交易历史.append(盈亏)
        if 盈亏 < 0:
            self.连亏次数 += 1
        else:
            self.连亏次数 = 0
        self.今日盈亏 += 盈亏
        if self.今日盈亏 > self.峰值:
            self.峰值 = self.今日盈亏
# ===== 市场温度 + 仓位建议 =====

def 市场温度(情绪数据: dict) -> dict:
    """计算当前市场温度(0-100)和建议仓位"""
    涨停 = int(情绪数据.get('涨停家数', 0) or 0)
    跌停 = int(情绪数据.get('跌停家数', 0) or 0)
    上涨 = int(情绪数据.get('上涨家数', 0) or 0)
    下跌 = int(情绪数据.get('下跌家数', 0) or 0)
    上涨比 = 上涨 / (上涨 + 下跌) if (上涨 + 下跌) > 0 else 0.5
    涨跌停比 = 涨停 / (跌停 + 1)
    
    温度 = min(涨停, 100) * 0.4 + 上涨比 * 40 + min(涨跌停比, 5) / 5 * 20
    温度 = max(0, min(100, round(温度)))
    
    建议 = ""
    if 温度 >= 75: 建议 = "🔥 高温(积极做多) | 仓位: 80-100%"
    elif 温度 >= 55: 建议 = "☀️ 常温(正常交易) | 仓位: 50-80%"
    elif 温度 >= 35: 建议 = "⛅ 低温(谨慎交易) | 仓位: 20-50%"
    else: 建议 = "❄️ 冰点(空仓防御) | 仓位: 0-20%"
    
    return {"温度": 温度, "建议": 建议, "上涨比": round(上涨比, 2)}


def 仓位建议(温度: int) -> str:
    """根据市场温度返回建议仓位百分比"""
    if 温度 >= 75: return "80-100%"
    elif 温度 >= 55: return "50-80%"
    elif 温度 >= 35: return "20-50%"
    return "0-20%"


def 最强机会榜(候选股列表: list, 前几名: int = 5) -> list:
    """今日最强机会榜（排序+原因）"""
    if not 候选股列表: return []
    排序 = sorted(候选股列表, key=lambda x: x.get('决策总分', 0), reverse=True)
    榜 = []
    for i, d in enumerate(排序[:前几名], 1):
        原因 = []
        if d.get('决策信号') in ('A', 'B'): 原因.append("引擎信号积极")
        if d.get('筹码集中度', 'N/A') != 'N/A':
            try: 
                if float(d.get('筹码集中度', 0)) > 50: 原因.append("筹码集中")
            except Exception as e:
                print(f"  ⚠️ 情绪周期.py: {e}")
        if d.get('形态分析', '') in ('看涨',): 原因.append("形态看涨")
        榜.append({"排名": i, "代码": d.get('代码',''), "名称": d.get('名称',''),
            "总分": d.get('决策总分',0), "信号": d.get('决策信号',''), "原因": " | ".join(原因) if 原因 else "量化评分靠前"})
    return 榜


# ===== 仓位管理引擎 =====

def 仓位建议_综合(市场温度: int, 个股评分: float, 信号: str,
               是主线: bool = False, 是龙头: bool = False) -> dict:
    """综合市场温度、个股评分、信号强弱、主线龙头加成，计算建议仓位"""
    温度系数 = 1.0 if 市场温度 >= 75 else (0.7 if 市场温度 >= 55 else (0.4 if 市场温度 >= 35 else 0.15))
    评分系数 = min(个股评分 / 100, 1.0)
    信号乘数 = {"A": 1.0, "B": 0.6, "C": 0.2, "D": 0.0}.get(信号, 0.1)
    主线加成 = 1.2 if 是主线 else 1.0
    龙头加成 = 1.3 if 是龙头 else 1.0
    
    仓位 = 评分系数 * 温度系数 * 信号乘数 * 主线加成 * 龙头加成
    仓位 = max(0, min(1, round(仓位, 2)))
    
    return {"建议仓位": "%.0f%%" % (仓位 * 100), "仓位系数": 仓位,
            "明细": "温度%.0f×评分%.0f×信号%s×主线%.1f×龙头%.1f=%.0f%%" % (
                市场温度, 个股评分, 信号, 主线加成, 龙头加成, 仓位 * 100)}


# ===== 组合风险分析 =====

def 组合风险分析(候选股列表: list) -> list:
    """检查候选股的行业集中度，返回预警列表"""
    if not 候选股列表: return []
    from collections import Counter
    板块计数 = Counter()
    for d in 候选股列表:
        if isinstance(d, dict):
            板块计数[d.get('板块', '未知')] += 1
    if not 板块计数: return []
    
    总 = sum(板块计数.values())
    预警 = []
    for 板块, 数量 in 板块计数.most_common():
        占比 = 数量 / 总 * 100
        if 占比 >= 50 and 数量 >= 3:
            预警.append("🔴 %s占比%.0f%%，严重集中" % (板块, 占比))
        elif 占比 >= 30 and 数量 >= 2:
            预警.append("🟡 %s占比%.0f%%，注意分散" % (板块, 占比))
    return 预警


# ===== 龙头识别 =====

def 龙头识别(热门板块: list, 候选股列表: list = None) -> list:
    """识别每个热门板块的龙一龙二"""
    if not 热门板块: return []
    龙头 = []
    for b in 热门板块[:5]:
        领涨 = b.get('领涨股', '')
        领涨涨幅 = b.get('领涨股涨跌幅', 0)
        if 领涨:
            龙头.append({"板块": b.get('名称',''), "龙一": 领涨, "涨幅": "%.1f%%" % 领涨涨幅,
                        "龙二": "", "地位": "龙头"})
    return 龙头


# ===== 策略解释器 =====

def 评分明细(评分结果: dict) -> list:
    """格式化评分明细，显示每个维度的得分和权重贡献"""
    if not 评分结果 or not isinstance(评分结果, dict): return []
    lines = []
    for key in ["资金面", "趋势结构", "量价结构", "动量系统", "情绪/板块", "基本面"]:
        v = 评分结果.get(key, {})
        if isinstance(v, dict) and '得分' in v:
            得分 = v.get('得分', 0)
            权重 = v.get('权重', 0)
            lines.append({"维度": key, "得分": 得分, "权重": "%.0f%%" % (权重 * 100),
                         "贡献": "%.1f" % (得分 * 权重)})
    return lines
