# ===== 情绪周期模型 =====
def 判断情绪周期(情绪数据: dict) -> dict:
    """判断当前市场情绪周期阶段
    输入: get_market_emotion() 返回的dict
    输出: {'周期': str, '评分': int, '建议': str}
    """
    涨停 = int(情绪数据.get('涨停家数', 0) or 0)
    跌停 = int(情绪数据.get('跌停家数', 0) or 0)
    上涨 = int(情绪数据.get('上涨家数', 0) or 0)
    下跌 = int(情绪数据.get('下跌家数', 0) or 0)
    全市场 = int(情绪数据.get('全市场股票', 1) or 1)
    
    上涨比 = 上涨 / (上涨 + 下跌) if (上涨 + 下跌) > 0 else 0.5
    涨跌停比 = 涨停 / (跌停 + 1)
    
    # 冰点：涨停极少，跌停多，普跌
    if 涨停 < 8 and 上涨比 < 0.3:
        周期 = '冰点'; 评分 = 5
        建议 = '极度冰点，空仓观望'
    elif 涨停 < 15 and 上涨比 < 0.35:
        周期 = '冰点'; 评分 = 10
        建议 = '情绪冰点，禁止交易'
    # 修复：涨停增加，情绪回暖
    elif 涨停 < 30 and 上涨比 > 0.35:
        周期 = '修复'; 评分 = 35
        建议 = '情绪修复，轻仓试探'
    elif 涨停 < 50 and 上涨比 > 0.40:
        周期 = '修复'; 评分 = 45
        建议 = '修复延续，关注龙头首板'
    # 发酵：涨停扩散，赚钱效应出现
    elif 涨停 < 65 and 涨跌停比 > 3:
        周期 = '发酵'; 评分 = 60
        建议 = '情绪发酵，可积极交易'
    elif 涨停 < 80 and 上涨比 > 0.50:
        周期 = '发酵'; 评分 = 70
        建议 = '发酵扩散，跟随主线'
    # 主升：涨停爆发，情绪极致
    elif 涨停 >= 80 and 上涨比 > 0.50:
        周期 = '主升'; 评分 = 85
        建议 = '情绪主升，全力做多'
    elif 涨停 >= 100:
        周期 = '主升'; 评分 = 95
        建议 = '情绪高潮，注意分歧'
    # 退潮：高位分歧，亏钱效应
    elif 涨跌停比 < 1.5 and 下跌 > 上涨:
        周期 = '退潮'; 评分 = 20
        建议 = '情绪退潮，减仓防御'
    else:
        周期 = '分化'; 评分 = 30
        建议 = '市场分化，精选个股'
    
    return {'周期': 周期, '评分': 评分, '建议': 建议,
            '涨停': 涨停, '跌停': 跌停, '上涨比': round(上涨比, 2)}


