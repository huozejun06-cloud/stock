def 批量新闻检查(候选股列表: list) -> list:
    """对一批候选股并行检查新闻情绪
    输入：[{代码, 名称}, ...]
    输出：[{代码, 名称, 新闻情绪}, ...]
    """
    if not 候选股列表:
        return []

    print(f"\n📰 正在检查 {len(候选股列表)} 只候选股的新闻情绪...")

    结果 = []
    for i, 股 in enumerate(候选股列表):
        code = 股.get("代码", 股) if isinstance(股, dict) else 股
        name = 股.get("名称", code) if isinstance(股, dict) else code

        print(f"  {i+1}/{len(候选股列表)} {code} {name}...")
        新闻 = 搜索个股新闻(code, name)
        if 新闻:
            情绪 = 分析新闻情绪(新闻)
            print(f"    → 情绪: {情绪['情绪']} | 新闻: {len(新闻)}条")
        else:
            情绪 = {"情绪": "中性", "置信度": 0.5, "关键新闻": [], "一票否决": False, "摘要": "未搜索到相关新闻"}
            print(f"    → 无相关新闻")

        结果.append({
            "代码": code,
            "名称": name,
            "新闻情绪": 情绪,
            "新闻条数": len(新闻),
        })
        time.sleep(0.5)  # 礼貌间隔

    print(f"  ✅ 新闻情绪检查完成")
    return 结果


