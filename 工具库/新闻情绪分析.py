def 搜索个股新闻(股票代码: str, 股票名称: str) -> list:
    """多源搜索一只股票的相关新闻"""
    所有新闻 = []

    # 源1：百度新闻（A股相关）
    try:
        from 工具库.新闻搜索 import 搜索新闻
        新闻 = 搜索新闻(f"{股票名称} {股票代码}")
        所有新闻.extend(新闻)
        time.sleep(0.3)
    except Exception as e:
        print(f"    ⚠️ 百度新闻搜索失败: {e}")

    # 源2：国际财经新闻（板块级）
    try:
        from 工具库.新闻搜索 import 搜索国际财经新闻
        新闻 = 搜索国际财经新闻(股票名称, 5)
        所有新闻.extend(新闻)
        time.sleep(0.3)
    except Exception as e:
        print(f"    ⚠️ 国际财经搜索失败: {e}")

    return 所有新闻


def 分析新闻情绪(新闻列表: list) -> dict:
    """对新闻列表进行情绪分析
    返回：{情绪: 负面/中性/正面, 置信度: 0-1, 关键新闻: [...], 一票否决: bool}
    """
    if not 新闻列表:
        return {"情绪": "中性", "置信度": 0.5, "关键新闻": [], "一票否决": False, "摘要": "无相关新闻"}

    总负面分 = 0
    总正面分 = 0
    关键新闻列表 = []

    for 新闻 in 新闻列表:
        标题 = 新闻.get("标题", "") + " " + 新闻.get("内容", "")
        标题_lower = 标题.lower()

        # 检查一票否决关键词
        for kw in 一票否决关键词:
            if kw in 标题:
                return {
                    "情绪": "重大负面",
                    "置信度": 1.0,
                    "关键新闻": [{"标题": 新闻.get("标题", ""), "原因": f"触发一票否决关键词: {kw}"}],
                    "一票否决": True,
                    "摘要": f"⚠️ 触发一票否决: {kw}",
                }

        # 统计负面/正面关键词命中
        负面命中 = [kw for kw in 负面关键词 if kw in 标题]
        正面命中 = [kw for kw in 正面关键词 if kw in 标题]

        if 负面命中:
            总负面分 += len(负面命中)
            关键新闻列表.append({
                "标题": 新闻.get("标题", "")[:60],
                "负面关键词": 负面命中[:3],
                "来源": 新闻.get("来源", ""),
            })

        if 正面命中:
            总正面分 += len(正面命中)

    # 计算综合情绪
    净情绪 = 总负面分 - 总正面分

    if 净情绪 >= 3 or (总负面分 > 0 and 总正面分 == 0):
        情绪 = "负面"
        置信度 = min(0.5 + 净情绪 * 0.1, 0.95)
    elif 净情绪 <= -3:
        情绪 = "正面"
        置信度 = min(0.5 + abs(净情绪) * 0.1, 0.9)
    else:
        情绪 = "中性"
        置信度 = 0.5

    return {
        "情绪": 情绪,
        "置信度": round(置信度, 2),
        "负面分数": 总负面分,
        "正面分数": 总正面分,
        "关键新闻": 关键新闻列表[:3],
        "一票否决": False,
        "摘要": _生成摘要(情绪, 置信度, 关键新闻列表),
    }


def _生成摘要(情绪: str, 置信度: float, 关键新闻: list) -> str:
    """生成新闻情绪摘要"""
    if 情绪 == "负面":
        lines = [f"🔴 负面情绪 (置信度{置信度:.0%})"]
        for n in 关键新闻[:2]:
            lines.append(f"   📰 {n['标题'][:50]}")
            lines.append(f"      关键词: {', '.join(n['负面关键词'][:3])}")
        return "\n".join(lines)
    elif 情绪 == "正面":
        return f"🟢 正面情绪 (置信度{置信度:.0%})"
    else:
        return "⚪ 中性情绪"


def 信号修正(决策引擎结果: dict, 新闻情绪: dict) -> dict:
    """根据新闻情绪修正决策引擎的信号
    规则：
    - 一票否决 → D (不论引擎给什么)
    - 负面 + B → 降为 C
    - 负面 + C → 降为 D
    - 正面 + C → 升为 B (但B不升A)
    - 正面 + B → 维持 B
    - 中性 → 维持原信号
    """
    if not 决策引擎结果:
        return 决策引擎结果

    原信号 = 决策引擎结果.get("信号", "C")
    原信号名 = 决策引擎结果.get("信号名称", "观望")
    情绪 = 新闻情绪.get("情绪", "中性")

    新信号 = 原信号
    新信号名 = 原信号名
    修理工序 = []

    # 一票否决：直接 D
    if 新闻情绪.get("一票否决"):
        return {
            **决策引擎结果,
            "信号": "D",
            "信号名称": "禁止交易",
            "理由": 决策引擎结果.get("理由", []) + [f"新闻一票否决: {新闻情绪['摘要']}"],
            "一句话结论": f"⛔ 新闻情绪触发一票否决，禁止交易",
        }

    # 负面情绪 → 降级
    if 情绪 == "重大负面":
        新信号 = "D"
        新信号名 = "禁止交易"
        修理工序.append(f"重大负面新闻 → 一票否决 D")
    elif 情绪 == "负面":
        if 原信号 == "A":
            新信号 = "B"
            新信号名 = "轻仓试探"
            修理工序.append(f"负面新闻 → A降为B")
        elif 原信号 == "B":
            新信号 = "C"
            新信号名 = "观望"
            修理工序.append(f"负面新闻 → B降为C")
        elif 原信号 == "C":
            新信号 = "D"
            新信号名 = "禁止交易"
            修理工序.append(f"负面新闻 → C降为D")
    elif 情绪 == "正面":
        if 原信号 == "C":
            新信号 = "B"
            新信号名 = "轻仓试探"
            修理工序.append(f"正面新闻 → C升为B")
        # B不升A，防追高

    # 组装结果
    修正后 = dict(决策引擎结果)
    修正后["信号"] = 新信号
    修正后["信号名称"] = 新信号名
    修正后["新闻情绪修正"] = {
        "原信号": 原信号,
        "新信号": 新信号,
        "修理工序": 修理工序,
        "新闻情绪": 新闻情绪,
    }

    if 修理工序:
        修正后["理由"] = 决策引擎结果.get("理由", []) + 修理工序 + [新闻情绪.get("摘要", "")]
        修正后["一句话结论"] = f"{'🟢' if 新信号=='A' else '🟡' if 新信号=='B' else '🟠' if 新信号=='C' else '🔴'} {新信号}({新信号名}) | 新闻情绪: {情绪}"

    return 修正后


