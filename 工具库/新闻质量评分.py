"""工具库/新闻质量评分.py — 新闻来源加权+时效性+去重"""
import time
来源权重 = {"巨潮公告":95,"交易所":95,"公司公告":90,"财联社":80,"华尔街见闻":75,
           "东方财富":70,"新浪财经":65,"百度新闻":60,"腾讯新闻":60,"新浪国际":55,"自媒体":30}

def 新闻质量评分(新闻列表: list) -> dict:
    """对新闻列表评分，返回综合质量分(0-100)和重要新闻"""
    if not 新闻列表: return {"综合质量":50,"重要新闻":[],"去重数":0}
    seen = set(); 总分 = 0; 重要 = []
    for n in 新闻列表:
        title = n.get("标题","")
        if title in seen: continue
        seen.add(title); src = n.get("来源","")
        w = 来源权重.get(src, 50)
        总分 += w
        if w >= 70: 重要.append(n)
    去重数 = len(新闻列表) - len(seen)
    return {"综合质量":min(100, 总分/len(seen)/100*100+20) if seen else 50,
            "重要新闻":重要[:3],"去重数":去重数,"来源统计":{s:来源权重.get(s,50) for s in set(n.get("来源","") for n in 新闻列表)}}
