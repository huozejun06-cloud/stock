from datetime import datetime
from 工具库.数据源管理器 import get_manager


def 获取竞价排名(数量: int = 30) -> dict:
    """
    获取集合竞价排名数据

    Args:
        数量: 返回前N只，默认30

    Returns:
        dict: {
            "时间": str,
            "上证开盘": float,
            "深证开盘": float,
            "创业板开盘": float,
            "涨停数": int,
            "跌停数": int,
            "涨幅前30": list[dict],
            "全市场": int,
        }
    """
    mgr = get_manager()
    df = mgr.get_all_market_stocks()
    时间 = datetime.now().strftime('%H:%M:%S')

    if df.empty:
        return {
            "时间": 时间,
            "上证开盘": 0, "深证开盘": 0, "创业板开盘": 0,
            "涨停数": 0, "跌停数": 0,
            "涨幅前30": [],
            "全市场": 0,
        }

    # 按涨跌幅排序取前N
    涨幅前N = df.nlargest(数量, '涨跌幅').to_dict('records')

    # 计算涨停/跌停数
    涨停数 = len(df[df['涨跌幅'] >= 9.8]) if '涨跌幅' in df.columns else 0
    跌停数 = len(df[df['涨跌幅'] <= -9.8]) if '涨跌幅' in df.columns else 0

    # 大盘开盘数据（通过数据源管理器获取指数行情）
    上证开盘 = 0
    深证开盘 = 0
    创业板开盘 = 0
    try:
        index_df = mgr.get_index_realtime()
        if not index_df.empty:
            if '上证' in index_df['名称'].values:
                上证开盘 = index_df[index_df['名称'] == '上证']['涨跌幅'].values[0]
            if '深证' in index_df['名称'].values:
                深证开盘 = index_df[index_df['名称'] == '深证']['涨跌幅'].values[0]
            if '创业板' in index_df['名称'].values:
                创业板开盘 = index_df[index_df['名称'] == '创业板']['涨跌幅'].values[0]
    except Exception:
        pass

    return {
        "时间": 时间,
        "上证开盘": 上证开盘,
        "深证开盘": 深证开盘,
        "创业板开盘": 创业板开盘,
        "涨停数": int(涨停数),
        "跌停数": int(跌停数),
        "涨幅前30": 涨幅前N,
        "全市场": len(df),
    }


def 格式化排名(排名数据: dict) -> str:
    """将竞价排名数据格式化为可读文本"""
    if not 排名数据 or not 排名数据.get('涨幅前30'):
        return "暂无竞价数据"

    文本 = "集合竞价排名（{}）\n".format(排名数据['时间'])
    文本 += "=" * 50 + "\n"
    文本 += "上证开盘: {:+.2f}%  |  ".format(排名数据['上证开盘'])
    文本 += "深证开盘: {:+.2f}%  |  ".format(排名数据['深证开盘'])
    文本 += "创业板开盘: {:+.2f}%\n".format(排名数据['创业板开盘'])
    文本 += "涨停: {}  |  跌停: {}  |  全市场: {}只\n".format(
        排名数据['涨停数'], 排名数据['跌停数'], 排名数据['全市场'])
    文本 += "=" * 50 + "\n"
    文本 += "{:<6}{:<10}{:<12}{:<12}{:<10}\n".format("排名", "代码", "名称", "开盘涨幅", "量比")
    文本 += "-" * 50 + "\n"

    for i, 股 in enumerate(排名数据['涨幅前30'][:20], 1):
        名称 = 股.get('名称', '')[:6]
        涨幅 = 股.get('涨跌幅', 0)
        量比 = 股.get('量比', 0)
        文本 += "{:<6}{:<10}{:<12}{:+.2f}%{:<6}{:<10}\n".format(
            i, 股.get('代码', ''), 名称, 涨幅, '', 量比)

    return 文本


if __name__ == "__main__":
    数据 = 获取竞价排名(10)
    print(格式化排名(数据))
